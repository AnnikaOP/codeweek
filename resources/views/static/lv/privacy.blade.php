            <h1 style="margin-bottom:10px;">JŪSU PERSONAS DATU AIZSARDZĪBA</h1>

            <h3><strong>1. Ievads</strong></h3>

            <p>Eiropas Komisija (turpmāk tekstā “Komisija”) ir apņēmusies aizsargāt jūsu personas datus un cienīt jūsu privātumu. Komisija vāc un apstrādā personas datus saskaņā ar Eiropas Parlamenta un Padomes 2018. gada 23. oktobra Regulu (ES) 2018/1725 par fizisku personu aizsardzību attiecībā uz personas datu apstrādi Savienības iestādēs, struktūrās, birojos un aģentūrās un par šādu datu brīvu apriti (ar ko atceļ Regulu (EK) Nr. 45/2001).</p>

            <p>Šajā paziņojumā par konfidencialitāti ir paskaidrots datu vākšanas un apstrādes iemesls, veids, kādā mēs vācam, apstrādājam datus un nodrošinām visu sniegto personas datu aizsardzību, veids, kādā šī informācija tiek izmantota, un tas, kādas tiesības jūs varat izmantot attiecībā uz jūsu personas datiem (piekļuves, labošanas, bloķēšanas u. c. tiesības). Tajā ir arī sniegta tā atbildīgā datu pārziņa kontaktinformācija, sazinoties ar kuru jūs varat izmantot jūsu tiesības, kā arī datu aizsardzības speciālista un Eiropas Datu aizsardzības uzraudzītāja kontaktinformācija.</p>

            <p>Šis paziņojums par konfidencialitāti attiecas uz personas datu vākšanu un publicēšanu publiski pieejamajā tīmekļa vietnē Codeweek.eu attiecībā uz personām, kuras darbojas kā ES Programmēšanas nedēļas pasākumu kontaktpersonas (Programmēšanas nedēļas vēstnieki, izglītības ministriju koordinatori, vadošie skolotāji, kā arī darbību un pasākumu organizatori).</p>



            <h3><strong>2. Kāpēc mēs apstrādājam jūsu datus?</strong></h3>

            <p>Eiropas Komisija vāc un publicē jūsu personas informāciju, lai ļautu ieinteresētajām organizācijām un personām vieglāk identificēt kontaktpersonas. Reālu personu norādīšana par kontaktpersonām ir labākais un efektīvākais veids, kā nodrošināt ieinteresētajiem cilvēkiem iespēju sazināties ar Komisijas dienestiem.</p>

            <p>Jūsu personas dati netiks izmantoti nekādai automatizētai lēmumu pieņemšanai, tostarp profilēšanai.</p>




            <h3><strong>3. Uz kāda juridiskā pamata mēs apstrādājam jūsu personas datus?</strong></h3>

            <p>Personas datu apstrādes darbības nolūkā publicēt kontaktpersonas ir likumīgas saskaņā ar Regulas (ES) 2018/1725 5. panta 1. punkta d) apakšpunktu, jo jūs devāt savu piekrišanu jūsu personas datu apstrādei tīmekļa veidlapā vai kad jūs piekritāt, ka mēs izmantojam jūsu e-pasta adresi un lietotājvārdu, ja jūs reģistrējāties, izmantojot sociālo tīklu.</p>



            <h3><strong>4. Kādus personas datus mēs vācam un apstrādājam?</strong></h3>

            <p>Savāktie personas dati ir informācija, kas ļauj vieglāk identificēt jūs kā kontaktpersonu un padara jūs redzamāku sabiedrībai, proti: tituls, vārds, uzvārds, amats, darba pasta un e-pasta adreses, tālruņa numurs, attēls, sociālo mediju konts, biogrāfija.</p>

            <p>Jūs sniedzāt šos datus brīvprātīgi, aizpildot pieteikuma veidlapu.</p>

            <p>Noteiktu personas datu sniegšana ir obligāta, lai varētu publicēt pasākumus un/vai kontaktinformāciju tīmekļa vietnē codeweek.eu. Ja jūs nesniedzat savus personas datus, jūsu pasākums netiks publicēts un/vai jūs nevarēsiet piedalīties iepriekš minētajos tīklos.</p>

            <p>Visu citu personas datu sniegšana ir brīvprātīga.</p>



            <h3><strong>5. Cik ilgi mēs glabājam jūsu personas datus?</strong></h3>

            <p>Komisija glabā jūsu personas datus tikai tik ilgi, cik nepieciešams, lai īstenotu vākšanas vai apstrādes mērķi, kas norādīts 2. punktā, proti, tik ilgi, cik ilgi jūs darbojaties kā kontaktpersona.</p>

            <p>Jūsu personas dati tiks dzēsti no publiski pieejamās tīmekļa vietnes, tiklīdz jūs beigsiet pildīt kontaktpersonas pienākumus, izņemot gadījumu, ja jūs piekritāt jūsu iekļaušanai datubāzē turpmākiem pasākumiem.</p>



            <h3><strong>6. Kā mēs aizsargājam jūsu personas datus?</strong></h3>

            <p>Visus personas datus elektroniskā formātā (e-pasta ziņojumi, dokumenti, datubāzes, augšupielādētās datu kopas u. c.) glabā Eiropas Komisijas vai tās darbuzņēmēja serveros. Visas apstrādes darbības veic saskaņā ar Komisijas 2017. gada 10. janvāra Lēmumu (ES, <i>Euratom</i>) 2017/46 par komunikācijas un informācijas sistēmu drošību Eiropas Komisijā.</p>

            <p>Komisijas darbuzņēmējiem ir saistošs īpašs līguma noteikums par jebkādām jūsu personas datu apstrādes darbībām Komisijas uzdevumā, kā arī konfidencialitātes pienākumi, kas izriet no Vispārīgās datu aizsardzības regulas (Regula (ES) 2016/679) —“VDAR”) transponēšanas ES dalībvalstīs.</p>

            <p>Lai aizsargātu jūsu personas datus, Komisija ir izveidojusi dažādus tehniskus un organizatoriskus pasākumus. Tehniskie pasākumi ietver attiecīgas darbības attiecībā uz drošību tiešsaistē, datu zuduma risku, datu pārveidošanu vai neatļautu piekļuvi tiem, ņemot vērā risku, ko rada apstrāde un apstrādāto personas datu veids. Organizatoriski pasākumi ietver piekļuves personas datiem ierobežošanu, piešķirot piekļuves tiesības tikai pilnvarotām personām, kurām ir likumīga vajadzība zināt, lai varētu īstenot attiecīgo apstrādes darbību.</p>




            <h3><strong>7. Kam ir piekļuve jūsu datiem un kam atklāj šos datus?</strong></h3>

            <p>Piekļuvi jūsu datiem sniedz tikai Komisijas darbiniekiem, kuri atbild par apstrādes darbības veikšanu, un pilnvarotiem darbiniekiem saskaņā ar principu “vajadzība zināt”. Šādi darbinieki ievēro ar tiesību aktiem noteiktas vienošanās un attiecīgos gadījumos arī papildu vienošanās par konfidencialitāti.</p>

            <p>Konkrēti, visiem jūsu sniegtajiem personas datiem var piekļūt tīmekļa vietņu administratori (Komisijas darbinieki), kā arī citi Komisijas darbinieki saskaņā ar principu “vajadzība zināt”. . Tāpat jūsu personas informācija un informācija par pasākumiem tiks sniegta ES programmēšanas nedēļas vēstnieku un izglītības koordinatoru tīklu dalībniekiem, lai organizētu vietējos pasākumus vai nodrošinātu saziņu pēc pasākumiem.</p>

            <p>Lai padarītu kontaktpersonas redzamākas, jūsu personas datus publicē bez piekļuves ierobežojumiem publiskajā tīmekļa vietnē: https://codeweek.eu.</p>

            <p><i>Datu nosūtīšana trešām personām</i></p>

            <p>Mūsu vāktā informācija netiks nodota trešām personām, izņemot tādā apmērā un nolūkā, kādā šādu nodošanu no mums var prasīt tiesību akti.</p>




            <h3><strong>8. Kādas ir jūsu tiesības un kā jūs varat tās izmantot?</strong></h3>

            <p>Jums ir konkrētas tiesības kā “datu subjektam” saskaņā ar Regulas (ES) 2018/1725 III nodaļu (14. –25. pantu), jo īpaši tiesības piekļūt jūsu personas datiem, tiesības pieprasīt to labošanu vai dzēšanu, kā arī tiesības ierobežot jūsu personas datu apstrādi. Attiecīgos gadījumos jums ir arī tiesības iebilst pret personas datu apstrādi vai tiesības uz datu pārnesamību.</p>
            <p>Jūs esat piekritis(-usi) sniegt jūsu personas datus mums šīs apstrādes darbības veikšanai un jūs varat jebkurā laikā atsaukt jūsu piekrišanu, paziņojot par to datu pārzinim. Piekrišanas atsaukšana neietekmēs pirms atsaukšanas veiktās apstrādes likumību.</p>
            <p>Jūs varat izmantot jūsu tiesības, sazinoties ar datu pārzini vai, domstarpību gadījumā, ar datu aizsardzības speciālistu. Ja nepieciešams, jūs varat arī vērsties pie Eiropas Datu aizsardzības uzraudzītāja. Viņu kontaktinformācija ir sniegta turpmāk 9. sadaļā.</p>



