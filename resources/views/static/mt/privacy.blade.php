            <h1 style="margin-bottom:10px;">PROTEZZJONI TAD-DATA PERSONALI TIEGĦEK</h1>

            <h3><strong>1. Introduzzjoni</strong></h3>

            <p>Il-Kummissjoni Ewropea (iktar ’il quddiem “il-Kummissjoni”) hija impenjata li tipproteġi d-data personali tiegħek u li tirrispetta l-privatezza tiegħek. Il-Kummissjoni tiġbor u ulterjorment tipproċessa data personali skont ir-Regolament (UE) 2018/1725 tal-Parlament Ewropew u tal-Kunsill tat‑23 ta’ Ottubru 2018 dwar il-protezzjoni ta’ persuni fiżiċi fir-rigward tal-ipproċessar ta’ data personali mill-istituzzjonijiet, korpi, uffiċċji u aġenziji tal-Unjoni u dwar il-moviment liberu ta’ tali data (tħassar ir-Regolament (KE) Nru 45/2001)</p>

            <p>Din id-dikjarazzjoni tal-privatezza tispjega r-raġuni għalfejn il-ġbir u l-ipproċessar, il-mod kif niġbru, nimmaniġġjaw u niżguraw il-protezzjoni tad-data personali pprovduta, kif tintuża dik l-informazzjoni u liema drittijiet tista’ teżerċita b’rabta mad-data personali tiegħek (id-dritt għall-aċċess, rettifika, imblukkar eċċ.) Tispeċifika wkoll id-dettalji tal-kuntatt tal-Kontrollur tad-Data responsabbli, li miegħu inti tista’ teżerċita d-drittijiet tiegħek, l-Uffiċjal tal-Protezzjoni tad-Data u l-Kontrollur Ewropew għall-Protezzjoni tad-Data.</p>

            <p>Din id-dikjarazzjoni dwar il-privatezza tikkonċerna l-ġbir u l-pubblikazzjoni ta’ data personali fuq is-sit web aċċessibbli pubblikament Codeweek.eu, ta’ persuni li jservu bħala punti ta’ kuntatt għall-attivitajiet ta; Code Week tal-UE (Ambaxxaturi ta’ Code Week, Koordinaturi tal-Ministeru għall-Edukazzjoni, Għalliema Ewlenin kif ukoll organizzaturi ta’ attivitajiet u avvenimenti).</p>



            <h3><strong>2. Għaliex nipproċessaw id-data tiegħek?</strong></h3>

            <p>Il-Kummissjoni Ewropea tiġbor u tippubblika l-informazzjoni personali tiegħek biex tiffaċilita l-identifikazzjoni ta’ punti ta’ kuntatt minn partijiet ikkonċernati jew ċittadini interessati. L-offerta ta’ persuni reali bħala punti ta’ kuntatt hija l-aħjar u l-iktar mod effiċjenti biex tiżgura li n-nies interessati jkunu jistgħu jagħmlu kuntatt mas-servizzi tal-Kummissjoni.</p>

            <p>Id-data personali tiegħek mhux se tintuża għal teħid ta’ deċiżjonijiet awtomatizzati inkluż it-tfassil ta’ profil.</p>




            <h3><strong>3. Fuq liema bażi(jiet) legali nipproċessaw id-data personali tiegħek</strong></h3>

            <p>L-operazzjonijiet ta’ pproċessar ta’ data personali għall-pubblikazzjoni ta’ punti ta’ kuntatt huma legali skont l-Artikolu 5(1)(d) tar-Regolament (UE) 2018/1725 minħabba li inti tajt il-kunsens tiegħek għall-ipproċessar tad-data personali tiegħek permezz ta’ formola web jew meta int qbilt li aħna nużaw l-email u l-identifikatur tal-utent tiegħek jekk tkun irreġistrajt permezz ta’ netwerk soċjali.</p>



            <h3><strong>4. Liema data personali niġbru u ulterjorment nipproċessaw?</strong></h3>

            <p>Id-data personali miġbura hija informazzjoni li tagħmel l-identifikazzjoni tiegħek bħala punt ta’ kuntatt aktar faċli u żżid il-viżibbiltà tiegħek għall-pubbliku, jiġifieri: titolu, isem, kunjom, pożizzjoni, indirizzi professjonali tal-posta u tal-posta elettronika, numru tat-telefon, stampa, kont tal-midja soċjali, bijografija.</p>

            <p>Int ipprovdejt din id-data personali fuq bażi volontarja, billi mlejt il-formola tal-applikazzjoni.</p>

            <p>Il-provvista ta’ ċerta data personali hija obbligatorja għall-pubblikazzjoni ta’ attivitajiet u/jew informazzjoni ta’ kuntatt possibbli fuq is-sit web codeweek.eu. Jekk ma tipprovdix id-data personali tiegħek, l-attività tiegħek ma tiġix ippubblikata, u/jew inti ma tkunx tista’ tieħu sehem fin-netwerks imsemmija qabel.</p>

            <p>Int tipprovdi d-data personali l-oħra kollha fuq bażi volontarja.</p>



            <h3><strong>5. Għal kemm żmien inżommu d-data personali tiegħek?</strong></h3>

            <p>Il-Kummissjoni żżomm biss id-data personali tiegħek għall-żmien meħtieġ biex twettaq l-iskop ta’ ġbir jew ipproċessar ulterjuri deskritt fil-punt 2, jiġifieri sakemm inti sservi bħala punt ta’ kuntatt.</p>

            <p>Id-data personali tiegħek se titneħħa mis-sit web disponibbli pubblikament malli twaqqaf il-funzjoni tal-punt ta’ kuntatt tiegħek sakemm ma tkunx tajt il-ftehim tiegħek biex tkun inkluż fid-database għal attivitajiet futuri.</p>



            <h3><strong>6. Kif nipproteġu u nħarsu d-data personali tiegħek?</strong></h3>

            <p>Id-data personali kollha f’format elettroniku (posta elettronika, dokumenti, bażijiet tad-data, pakketti ta’ data mtella’, eċċ.) hija maħżuna fuq is-servers tal-Kummissjoni Ewropea jew tal-kuntrattur tagħha. L-operazzjonijiet kollha ta’ pproċessar qed jitwettqu skont id-Deċiżjoni tal-Kummissjoni (UE, Euratom) 2017/46 tal-10 ta’ Jannar 2017 dwar is-sigurtà tas-sistemi ta’ komunikazzjoni u informazzjoni fil-Kummissjoni Ewropea.</p>

            <p>Il-kuntratturi tal-Kummissjoni huma marbuta bi klawżola kuntrattwali speċifika għal kwalunkwe operazzjonijiet ta’ pproċessar tad-data tiegħek f’isem il-Kummissjoni, u mill-obbligi ta’ kunfidenzjalità li ġejjin mit-traspożizzjoni tar-Regolament Ġenerali dwar il-Protezzjoni tad-Data fl-Istati Membri tal-UE (ir-Regolament “GDPR” (UE) 2016/679).</p>

            <p>Sabiex tipproteġi d-data personali tiegħek, il-Kummissjoni daħħlet fis-seħħ numru ta’ miżuri tekniċi u organizzattivi. Il-miżuri tekniċi jinkludu azzjonijiet xierqa biex jindirizzaw is-sigurtà online, ir-riskju ta’ telf ta’ data, alterazzjoni ta’ data jew aċċess mhux awtorizzat, b’kunsiderazzjoni tar-riskju ppreżentat mill-ipproċessar u n-natura tad-data personali li qed tiġi pproċessata. Miżuri ta’ organizzazzjoni jinkludu restrizzjoni tal-aċċess għad-data personali biss għal persuni awtorizzati li għandhom bżonn leġittimu li jkunu jafu għall-finijiet ta’ din l-operazzjoni ta’ pproċessar.</p>




            <h3><strong>7. Min għandu aċċess għall-informazzjoni tiegħek u lil min tiġi żvelata?</strong></h3>

            <p>L-aċċess għad-data personali tiegħek huwa pprovdut lill-persunal tal-Kummissjoni responsabbli biex iwettaq din l-operazzjoni ta’ pproċessar u lill-persunal awtorizzat skont il-prinċipju ta’ “meħtieġ li tkun taf”. Tali persunal jimxi skont ftehim statutorju, u meta jkun meħtieġ, ma’ ftehim addizzjonali ta’ kunfidenzjalità.</p>

            <p>B’mod konkret, id-data personali kollha pprovduta minnek tista’ tiġi aċċessata mill-amministraturi tas-siti web (persunal tal-Kummissjoni), kif ukoll minn persunal ieħor tal-Kummissjoni fuq bażi ta’ meħtieġ li tkun taf. . Barra minn hekk, l-informazzjoni personali u l-informazzjoni dwar l-avvenimenti tiegħek se jiġu kondiviżi ma’ membri tal-Ambaxxaturi tal-Ġimgħa Ewropea tal-Ikkowdjar u netwerks ta’ koordinaturi Edukattivi għal organizzazzjoni ta’ attivitajiet lokali jew segwitu.</p>

            <p>Bil-għan li tiżdied il-viżibilità tal-punti ta’ kuntatt, id-data personali tiegħek hija ppubblikata mingħajr ebda limitazzjoni ta’ aċċess fuq is-sit web pubbliku: https://codeweek.eu.</p>

            <p><i>Rigward it-trasferiment ta’ data lil partijiet terzi</i></p>

            <p>L-informazzjoni li niġbru mhux se tingħata lil ebda parti terza, ħlief sal-punt u għall-iskop li aħna nistgħu nkunu meħtieġa li nagħmlu hekk bil-liġi.</p>




            <h3><strong>8. X’inhuma d-drittijiet tiegħek u kif tista’ teżerċitahom?</strong></h3>

            <p>Int għandek drittijiet speċifiċi bħala “suġġett tad-data” skont il-Kapitolu III (Artikoli 14-25) tar-Regolament (UE) 2018/1725, b’mod partikolari d-dritt li taċċessa, tirranġa jew tħassar id-data personali tiegħek u d-dritt li tillimita l-ipproċessar tad-data personali tiegħek. Fejn applikabbli, inti għandek ukoll id-dritt li toġġezzjona għall-ipproċessar jew id-dritt għall-portabbiltà tad-data.</p>
            <p>Inti tajt il-kunsens li tipprovdi d-data personali tiegħek lilna għall-operazzjoni ta’ pproċessar preżenti u tista’ tirtira l-kunsens tiegħek fi kwalunkwe ħin billi tinnotifika lill-Kontrollur tad-Data. L-irtirar ma jaffettwax il-legalità tal-ipproċessar li jkun sar qabel ma rtirajt il-kunsens.</p>
            <p>Tista’ teżerċita d-drittijiet tiegħek billi tikkuntattja lill-Kontrollur tad-Data, jew f’każ ta’ kunflitt lill-Uffiċjal tal-Protezzjoni tad-Data. Jekk meħtieġ, tista’ wkoll tindirizza lill-Kontrollur Ewropew għall-Protezzjoni tad-Data. L-informazzjoni ta’ kuntatt tagħhom hija mogħtija fl-Intestatura 9 hawn taħt.</p>



