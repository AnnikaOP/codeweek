                <h2>Cos&rsquo;&egrave; la settimana europea della programmazione?</h2>
                <p>La settimana europea della programmazione &egrave; un&rsquo;iniziativa che nasce dal basso gestita da volontari e sostenuta dalla Commissione europea. Chiunque (scuole, insegnanti, biblioteche, club di programmazione, imprese, autorit&agrave; pubbliche) pu&ograve; organizzare un&rsquo;attivit&agrave; #CodeWeek e aggiungerla alla mappa <a href="/events">codeweek.eu</a>.</p>


