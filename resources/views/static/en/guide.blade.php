                <h2>What is EU Code Week?</h2>
                <p>
                    EU Code Week is a grass-roots movement run by volunteers and supported by the European Commission.
                    Anyone – schools, teachers, libraries, code clubs, businesses, public authorities – can organise a
                    #CodeWeek activity and add it to the <a href="/events">codeweek.eu</a> map.

                </p>


                <h2>What do you need in order to organise an activity?</h2>
                <ul>
                    <li><strong>A group of people willing to learn.</strong> For example, your friends, kids, teenagers,
                        adults colleagues, friends, parents or grandparents. Remember, two is a group already!
                    </li>
                    <li><strong>Teachers or trainers</strong> who are familiar with the coding activity, and how to
                        teach and inspire others. The number depends on the type and size of the event.

                    </li>
                    <li><strong>A place to learn.</strong> Classrooms, libraries, conference rooms and various public
                        spaces all make a great event venue.

                    </li>
                    <li><strong>Computers and internet connection.</strong> Depending on your target group, you might
                        ask participants to bring their own laptops.
                    </li>

                    <strong>Coding unplugged.</strong> You actually don't need computers and internet connection to
                    learn computational thinking.
                    Take a look at our <a href="/training/coding-without-computers">Unplugged learning bit</a> to get
                    started.
                    </li>


                    <li><strong>Learning materials.</strong>

                        Show participants how fun it can be to create something on their own. Check out our <a
                                href="/resources">resources page</a>
                        and <a href="/training">learning bits</a> with video tutorials and lesson plans, and adjust them
                        to your group's
                        needs.
                    </li>


                    <li><strong>Register participants.</strong> If you have limited space available, you can use online
                        tools such as <a href="https://docs.google.com/forms/">Google forms</a> and <a
                                href="https://www.eventbrite.com/">Eventbrite</a> to register participants.
                    </li>
                    <li>Remember to <a href="/add">pin your activity</a> on the <a href="/events">Code Week map</a>!
                    </li>
                </ul>


                <h2>How to organise your activity?</h2>
                <ul>
                    <li>The format of your coding event is up to you, but we do recommend that you include some <strong>practical,
                            hands-on time</strong>, where participants can create on their own and/or tinker with some
                        hardware.
                    </li>
                    <li>

                        Make use of <strong>tools and technologies</strong> appropriate to your target group. We
                        recommend using <a href="http://codeweek.eu/resources/">freely
                            available open source material</a>.

                    </li>

                    <li>

                        Encourage participants to <strong>show and present</strong> what they have created to each other
                        at the end of your event.

                    </li>

                    <li>
                        <strong>Spread the news!</strong> Promote and share what you did during your activity on social
                        media by using
                        the #CodeWeek hashtag. You can also share on the <a
                                href="https://www.facebook.com/groups/774720866253044/">EU Code Week Teacher’s group</a>
                        and on Twitter
                        (<a href="https://twitter.com/CodeWeekEU">@CodeWeekEU</a>). Talk to your friends, fellow
                        educators, the local press, and make a press
                        release.
                    </li>

                    <li>Don't forget to <a href="/add">add your activity</a> on the <a href="/events">Code Week map</a>!
                    </li>


                </ul>


