            <h1 style="margin-bottom:10px;">OCHRANA VAŠICH OSOBNÍCH ÚDAJŮ</h1>

            <h3><strong>1. Úvod</strong></h3>

            <p>Evropská komise (dále jen „Komise“) se zavazuje chránit vaše osobní údaje a respektovat vaše soukromí. Komise osobní údaje shromažďuje a dále zpracovává v souladu s nařízením Evropského parlamentu a Rady (EU) 2018/1725 ze dne 23. října 2018 o ochraně fyzických osob v souvislosti se zpracováním osobních údajů orgány, institucemi a jinými subjekty Unie a o volném pohybu těchto údajů (a o zrušení nařízení (ES) č. 45/2001).</p>

            <p>Toto prohlášení o ochraně osobních údajů vysvětluje důvod pro shromažďování a zpracování osobních údajů, způsob, jakým osobní údaje shromažďujeme, jak s nimi nakládáme a jakým způsobem zajišťujeme jejich ochranu, jak vaše osobní údaje používáme a jaká máte v souvislosti s vašimi osobními údaji práva (právo na přístup, opravu, zamezení zpracování atd.). Prohlášení rovněž obsahuje kontaktní údaje na odpovědného správce údajů, u něhož můžete vykonat svá práva, na pověřence pro ochranu osobních a na evropského inspektora ochrany údajů.</p>

            <p>Toto prohlášení o ochraně osobních údajů se týká shromažďování a zveřejňování osobních údajů osob, které slouží jako kontaktní osoby v souvislosti s aktivitami Evropského týdne programování (ambasadoři Evropského týdne programování, koordinátoři ministerstev školství, vedoucí učitelé a organizátoři aktivit a událostí) na veřejně přístupné webové stránce Codeweek.eu.</p>



            <h3><strong>2. Proč vaše údaje zpracováváme?</strong></h3>

            <p>Evropská komise vaše osobní údaje shromažďuje a zpracovává za účelem usnadnění identifikace kontaktních osob zúčastněnými účastníky nebo občany. Skutečné kontaktní osoby jsou nejlepším a nejefektivnějším způsobem jak zajistit, aby se zainteresovaní lidé dostali do kontaktu se službami Komise.</p>

            <p>Vaše osobní údaje nebudou použity pro jakékoli automatizované rozhodování včetně profilování.</p>




            <h3><strong>3. Na jakém právním základě / jakých právních základech zpracováváme vaše osobní údaje</strong></h3>

            <p>Úkony zpracování osobních údajů za účelem zveřejnění kontaktních údajů jsou zákonné v souladu s čl. 5, odst. 1, písm. d) nařízení (EU) 2018/1725, jelikož jste skrze webový formulář nebo při udělení souhlasu s použitím vašeho e-mailu a uživatelského jména při registraci pomocí sociální sítě udělili souhlas se zpracováním vašich osobních údajů.</p>



            <h3><strong>4. Jaké osobní údaje shromažďujeme a dále zpracováváme?</strong></h3>

            <p>Shromážděné osobní údaje jsou informace, které usnadňují vaši identifikaci jakožto kontaktní osoby, a zvyšují vaši viditelnost pro veřejnost, jmenovitě: oslovení, jméno, příjmení, pozice, pracovní poštovní a e-mailová adresa, telefonní číslo, fotografie, účet na sociálních sítích, krátká biografie.</p>

            <p>Tyto osobní údaje jste poskytli dobrovolně vyplněním přihlášky.</p>

            <p>Poskytnutí určitých osobních údajů je povinné pro zveřejnění aktivit a/nebo kontaktních informací na webové stránce codeweek.eu. Pokud své osobní údaje neposkytnete, vaše aktivita nebude zveřejněna a/nebo nebudete schopni zapojit se do výše uvedených sítí.</p>

            <p>Veškeré ostatní osobní údaje poskytujete dobrovolně.</p>



            <h3><strong>5. Jak dlouho vaše osobní údaje uchováváme?</strong></h3>

            <p>Komise vaše osobní údaje uchovává pouze po dobu nutnou k naplnění účelu jejich shromáždění nebo dalšího zpracování popsaného v bodu 2, tedy po dobu, po kterou budete působit jako kontaktní osoba.</p>

            <p>Pokud neposkytnete souhlas se zařazením do databáze pro budoucí aktivity, vaše osobní údaje budou z veřejně dostupné webové stránky odstraněny, jakmile přestanete působit ve funkci kontaktní osoby.</p>



            <h3><strong>6. Jak vaše osobní údaje chráníme?</strong></h3>

            <p>Veškeré osobní údaje v elektronickém formátu (e-maily, dokumenty, databáze, nahrané datové dávky atd.) jsou ukládány buď na serverech Evropské komise, nebo na serverech jejího smluvního dodavatele. Veškeré úkony zpracování osobních údajů jsou prováděny v souladu s rozhodnutím Komise (EU, Euratom) 2017/46 ze dne 10. ledna 2017 o bezpečnosti komunikačních a informačních systémů v Evropské komisi.</p>

            <p>Smluvní dodavatelé Komise jsou vázáni specifickou smluvní doložkou o zpracování vašich osobních údajů jménem Komise a závazky o zachování důvěrnosti vyplývajícími z transpozice obecného nařízení o ochraně osobních údajů (nařízení (EU) 2016/679, „GDPR“) do právních předpisů členských států EU.</p>

            <p>Za účelem ochrany vašich osobních údajů Komise zavedla řadu technických a organizačních opatření. Technická opatření zahrnují náležité kroky k řešení online zabezpečení, rizika ztráty údajů, pozměnění údajů nebo neoprávněného přístupu k údajům a berou v potaz rizika spojená se zpracováním a povahou zpracovávaných osobních údajů. Organizační opatření zahrnují omezení přístupu k osobním údajům pouze pro oprávněné osoby s oprávněnou potřebou tyto údaje znát za účelem jejich zpracování.</p>




            <h3><strong>7. Kdo má přístup k vašim údajům a komu jsou poskytovány?</strong></h3>

            <p>Přístup k vašim osobním údajům je poskytnut pracovníkům Komise odpovědným za provádění úkonů zpracování a dalším oprávněným pracovníkům na základě jejich potřeby tyto údaje znát. Tito pracovníci jsou vázáni právními předpisy a v případě potřeby také doplňujícími dohodami o zachování důvěrnosti.</p>

            <p>Ke všem vámi poskytnutým osobním údajům mají přístup správci webových stránek Komise (pracovníci Komise) a další pracovníci Komise, kteří je potřebují znát. . Vaše osobní údaje a informace o akci budou za účelem organizace místních aktivit poskytnuty členům sítí ambasadorů a koordinátorů Evropského týdne programování.</p>

            <p>Za účelem zvýšení viditelnosti kontaktních osob budou vaše osobní údaje bez jakéhokoli omezení přístupnosti zveřejněny na webové stránce: https://codeweek.eu.</p>

            <p><i>K přenosu údajů třetím stranám</i></p>

            <p>Údaje, které shromažďujeme, nebudou poskytnuty jakékoli třetí straně, vyjma případů, kdy jsme tak povinni učinit ze zákona.</p>




            <h3><strong>8. Jaká jsou vaše práva a jak je můžete vykonat?</strong></h3>

            <p>Jakožto „subjekt údajů“ máte v souladu s kapitolou 3 (články 14–25) nařízení (EU) 2018/1725 specifická práva, konkrétně právo na přístup k vašim osobním údajům, právo na jejich opravu a právo na omezení jejich zpracování. V příslušných případech máte také právo vznést námitku vůči zpracování vašich osobních údajů nebo právo na jejich přenositelnost.</p>
            <p>Souhlasili jste s poskytnutím svých osobních údajů za popsaným účelem jejich zpracování. Tento souhlas můžete kdykoli odvolat prostřednictvím oznámení zaslaného správci údajů. Odvolání souhlasu nebude mít vliv na zákonnost zpracování provedeného před odvoláním souhlasu.</p>
            <p>Svá práva můžete nárokovat kontaktováním správce údajů, v případě sporu pak u pověřence pro ochranu osobních údajů. V případě potřeby můžete rovněž kontaktovat evropského inspektora ochrany údajů. Jejich kontaktní informace jsou uvedeny v oddílu 9 níže.</p>



