            <h1 style="margin-bottom:10px;">ZAŠTITA VAŠIH OSOBNIH PODATAKA</h1>

            <h3><strong>1. Uvod</strong></h3>

            <p>Evropska komisija (u daljem tekstu: Komisija) opredijeljena je za zaštitu vaših osobnih podataka i za poštovanje vaše privatnosti. Komisija prikuplja i dalje obrađuje osobne podatke u skladu sa Uredbom (EU) 2018/1725 Evropskog parlamenta i Vijeća od 23. oktobra 2018. o zaštiti fizičkih lica u pogledu obrade osobnih podataka od strane institucija, tijela, ureda i agencija Unije i o slobodnom kretanju takvih podataka (čime se poništava Uredba (EC) br. 45/2001).</p>

            <p>U ovoj izjavi o privatnosti pojašnjava se razlog za prikupljanje i obradu, način na koji mi prikupljamo i obrađujemo sve dostavljene osobne podatke, te osiguravamo njihovu zaštitu, kako se te informacije koriste i koja prava vi eventualno vršite u odnosu na svoje osobne podatke (pravo pristupa, korekcije, blokiranja, itd),  Njome se također preciziraju pojedinosti o odgovornom kontroloru podataka s kojim možete vršiti svoja prava, referentu za zaštitu podataka i evropskom nadzorniku za zaštitu podataka.</p>

            <p>Ova izjava o privatnosti tiče se prikupljanja i objavljivanja osobnih podataka na javno pristupačnoj internet stranici Codeweek.eu o licima koja služe kao tačke kontakta za aktivnosti u okviru Sedmice kodiranja EU (ambasadori Sedmice kodiranja, koordinatori u ministarstvima obrazovanja, vodeći nastavnici, kao i organizatori aktivnosti i dešavanja).</p>



            <h3><strong>2. Zašto mi obrađujemo vaše podatke?</strong></h3>

            <p>Evropska komisija prikuplja i objavljuje vaše osobne informacije kako bi olakšala interesnim stranama ili zainteresiranim građanima identificiranje tački kontakta. Nuđenje stvarnih osoba kao tački kontakta je najbolji i najefikasniji način da se osigura da zainteresirana lica stupe u kontakt sa službama Komisije.</p>

            <p>Vaši osobni podaci neće se koristiti ni za kakvo automatizirano odlučivanje, uključujući profiliranje.</p>




            <h3><strong>3. Na kom pravnom temelju (temeljima) obrađujemo vaše osobne podatke</strong></h3>

            <p>Operacije obrade na osobnim podacima za objavljivanje tački kontakta zakonite su po članu 5(1)(d) Uredbe (EU) 2018/1725, jer ste vi dali pristanak na obradu svojih osobnih podataka putem internet  formulara ili kada ste se složili da mi koristimo vašu e-poštu i korisničko ime ako ste se prijavljivali putem društvene mreže. </p>



            <h3><strong>4. Koje osobne podatke prikupljamo i dalje obrađujemo?</strong></h3>

            <p>Osobni podaci koji se prikupljaju su informacije kojima se vaša identifikacija kao tačke kontakta olakšava i kojima se povećava vaša vidljivost za javnost, naime: naslov, ime, prezime, pozicija, profesionalna poštanska adresa i adresa e-pošte, telefonski broj, slika, račun društvenih medija, biografija. </p>

            <p>Vi ste dali te osobne podatke na dobrovoljnoj osnovi popunjavanjem prijavnog formulara.</p>

            <p>Davanje određenih osobnih podataka obavezno je za objavljivanje aktivnosti i/ili kontakt informacija na internet stranici codeweek.eu.  Ako ne budete dali svoje osobne podatke, vaša aktivnost neće biti objavljena i/ili vi nećete moći učestvovati u gore navedenim mrežama.</p>

            <p>Sve ostale osobne podatke dajete na dobrovoljnoj osnovi.</p>



            <h3><strong>5. Koliko dugo držimo vaše osobne podatke?</strong></h3>

            <p>Komisija drži vaše osobne podatke samo onoliko dugo koliko je neophodno u svrhu prikupljanja ili daljnje obrade što je opisano u tački 2, naime onoliko dugo koliko vi služite kao tačka kontakta.</p>

            <p>Vaši osobni podaci će biti uklonjeni sa javno dostupne internet stranice čim vi prestanete biti na svojoj funkciji tačke kontakta, osim ako niste dali pristanak da budete uključeni u bazu podataka za buduće aktivnosti.</p>



            <h3><strong>6. Kako mi štitimo i čuvamo vaše osobne podatke?</strong></h3>

            <p>Svi osobni podaci u elektronskom obliku (e-pošta, dokumenti, baze podataka, učitane serije podataka, itd.) pohranjuju se na servere Evropske komisije ili njenog ugovornog izvođača. Sve operacije obrade vrše se u skladu sa Odlukom Komisije (EU, Euratom) 2017/46 od 10. januara 2017. o sigurnosti komunikacijskih i informacijskih sistema u Evropskoj komisiji.</p>

            <p>Ugovorni izvođači Komisije obavezani su posebnom ugovornom klauzulom po pitanju svih evenualnih operacija obrade vaših podataka u ime Komisije, te obavezama povjerljivosti koje proističu iz transpozicije Opšte uredbe o zaštiti podataka u državama članicama EU (Uredba "GDPR" (EU) 2016/679).</p>

            <p>U cilju zaštite vaših osobnih podataka, Komisija je uspostavila niz tehničkih i organizacionih mjera. Tehničke mjere uključuju odgovarajuće akcije u cilju rada na rješavanju online sigurnosti, rizika od gubljenja podataka, mijenjanja podataka ili neovlaštenog pristupa, uzimajući u obzir rizik koji je posljedica obrade i prirodu osobnih podataka koji se obrađuju. Organizacione mjere uključuju ograničavanje pristupa osobnim podacima samo na ovlaštena lica sa legitimnom potrebom da znaju koja je svrha ove operacije obrade.</p>




            <h3><strong>7. Ko ima pristup vašim podacima i kome se oni otkrivaju?</strong></h3>

            <p>Pristup vašim osobnim podacima je omogućen osoblju Komisije nadležnom za vršenje ove operacije obrade i ovlaštenom osoblju u skladu sa principom "potrebe za saznanjem". To osoblje pridržava se zakonskih, a po potrebi i dodatnih ugovora o povjerljivosti.</p>

            <p>Konkretno, svim osobnim podacima koje dostavite mogu pristupiti administratori internet stranica (osoblje Komisije), kao i drugo osoblje Komisije na temelju potrebe za saznanjem. . Osim toga, vaše osobne informacije i informacije o dešavanjima će se podijeliti sa članovima mreža ambasadora i obrazovnih koordinatora Sedmice kodiranja EU za organizaciju lokalnih aktivnosti ili naknadni rad na njima. </p>

            <p>U svrhu povećanja vidljivosti tački kontakta, vaši osobni podaci se objavljuju bez ikakvog ograničenja pristupa na javnoj internet stranici: https://codeweek.eu.</p>

            <p><i>Pitanje prijenosa podataka trećim licima</i></p>

            <p>Informacije koje mi prikupljamo neće se davati nijednom trećem licu, osim u onom obimu i u svrhu za koju to budemo po zakonu obavezni uraditi.</p>




            <h3><strong>8. Kakva su vaša prava i kako ih možete vršiti?</strong></h3>

            <p>Vi imate posebna prava kao "subjekt podataka" po Poglavlju III (članovi 14-25) Uredbe (EU) 2018/1725, a posebno pravo na pristup, korekciju ili brisanje svojih osobnih podataka, te pravo na ograničavanje obrade svojih osobnih podataka. Gdje je to relevatno, imate i pravo na ulaganje prigovora na obradu ili pravo na prenosivost podataka.</p>
            <p>Vi ste pristali na dostavljanje svojih osobnih podataka za postojeću operaciju obrade i možete u svako vrijeme povući svoj pristanak dostavom obavijesti kontroloru podataka. Povlačenjem se neće utjecati na zakonitost obrade koja je izvršena prije nego što ste vi povukli pristanak.</p>
            <p>Vi možete vršiti svoja prava tako što ćete kontaktirati kontrolora podataka, ili u slučaju sukoba, referenta za zaštitu podataka. U slučaju da je to neophodno, možete se obratiti i evropskom nadzorniku za zaštitu podataka. Njihove kontakt informacije date su pod Naslovom 9. u nastavku.</p>



