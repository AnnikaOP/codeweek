            <h1 style="margin-bottom:10px;">KİŞİSEL VERİLERİNİZİN KORUNMASI</h1>

            <h3><strong>1. Giriş</strong></h3>

            <p>Avrupa Komisyonu (bundan sonra ‘Komisyon’ olarak anılacaktır), kişisel verilerinizi korumayı ve gizliliğinize saygı duymayı taahhüt eder. Komisyon; Birlik kurumları, organları, ofisleri ve ajansları tarafından kişisel verilerin işlenmesine ilişkin gerçek kişilerin korunması ve bu tip verilerin serbest dolaşımı hakkındaki Avrupa Parlamentosu ve Konsey'in 23 Ekim 2018 tarihli (EU) 2018/1725 sayılı Tüzüğü'ne uygun olarak ((EC) No 45/2001 sayılı tüzük iptal edilerek) kişisel verileri toplar ve bunlara ileri işlem uygular.</p>

            <p>Gizlilik bildirimi; toplama ve işleme işlemlerinin nedenini, sağlanan tüm kişisel verileri toplama, işleme ve korunmasını sağlama şeklimizi, bu bilgilerin ne şekilde kullanıldığını ve kişisel verilerinize ilişkin hangi hakları (erişim hakkı, düzeltme, engelleme vb.) kullanabileceğinizi açıklar. Bildirim ayrıca, haklarınızı birlikte kullanabileceğiniz sorumlu Veri Denetçisi'nin, Veri Koruma Görevlisi'nin ve Avrupa Veri Koruma Süpervizörü'nün iletişim ayrıntılarını belirtir.</p>

            <p>Bu gizlilik bildirimi, herkesin açık şekilde erişebildiği Codeweek.eu web sitesinde, EU Code Week etkinlikleri için irtibat noktası olarak hizmet veren kişilerin (Code Week elçileri, Eğitim Bakanlığı Koordinatörleri, Lider Öğretmenler'in yanı sıra aktivite ve etkinlik organizatörleri) kişisel verilerinin toplanması ve yayımlanmasına ilişkindir.</p>



            <h3><strong>2. Verilerinizi neden işliyoruz?</strong></h3>

            <p>Avrupa Komisyonu, irtibat noktalarının proje katılımcıları veya ilgili vatandaşlar tarafından saptanmasını kolaylaştırmak amacıyla kişisel verilerinizi toplar ve yayımlar. Gerçek kişilerin irtibat noktası olarak sunulması, ilgili kişilerin Komisyon hizmetleri ile irtibata geçmesini sağlamak için en iyi ve en etkin yoldur.</p>

            <p>Kişisel verileriniz, profil oluşturma da dâhil olmak üzere hiçbir otomatik karar verme işlemi için kullanılmayacaktır.</p>




            <h3><strong>3. Kişisel verilerinizi hangi yasal zeminler üzerinde işliyoruz</strong></h3>

            <p>İrtibat noktalarının yayımlanması için kişisel veriler üzerinde yapılan işleme faaliyetleri, (EU) 2018/1725 sayılı Tüzüğün 5(1)(d) maddesi uyarınca yasaldır çünkü bir web formu üzerinden veya bir sosyal ağ ile kaydoluyorsanız e-postanızı ve kullanıcı adınızı kullanmamızı kabul ettiğinizde kişisel verilerinizin işlenmesine izin verdiniz.</p>



            <h3><strong>4. Hangi kişisel verileri topluyoruz ve ileri işlem uyguluyoruz?</strong></h3>

            <p>Toplanan kişisel veriler, kimliğinizi bir irtibat noktası olarak daha kolay hâle getiren ve herkes için görünürlüğünüzü artıran bilgilerdir, yani: unvan, ad, soyad, pozisyon, profesyonel posta ve e-posta adresleri, telefon numarası, resim, sosyal medya hesabı, biyografi.</p>

            <p>Bu kişisel verileri, başvuru formunu doldurarak gönüllülük esasına dayanarak sağladınız.</p>

            <p>Belli kişisel verilerin sağlanması, codeweek.eu web sitesindeki etkinliklerin ve/veya irtibat bilgilerinin yayımlanabilmesi için zorunludur. Kişisel verilerinizi sağlamazsanız, etkinliğiniz yayımlanmaz ve/veya bahsi geçen ağlarda yer alamazsınız.</p>

            <p>Diğer tüm kişisel verileri gönüllülük esasına dayanarak sağlarsınız.</p>



            <h3><strong>5. Kişisel verilerinizi ne kadar süreyle saklıyoruz?</strong></h3>

            <p>Komisyon yalnızca 2. bölümde açıklanan toplama veya ileri işlem amacını gerçekleştirmek üzere gereken süre kadar kişisel verilerinizi saklar, yani siz irtibat noktası olarak hizmet verdiğiniz sürece.</p>

            <p>Kişisel verileriniz, ilerideki etkinlikler için veri tabanında tutulmak üzere onay vermedikçe, siz irtibat noktası işlevinizi sonlandırdığınız anda herkese açık şekilde kullanılabilir web sitesinden kaldırılacaktır.</p>



            <h3><strong>6. Kişisel verilerinizi nasıl koruyoruz ve muhafaza ediyoruz?</strong></h3>

            <p>Elektronik biçimdeki tüm kişisel veriler (e-postalar, belgeler, veri tabanları, karşıya yüklenen veri grupları vb.) Avrupa Komisyonu veya ona ait yüklenicinin sunucularında saklanır. Tüm işleme faaliyetleri, Avrupa Komisyonu içerisindeki iletişim ve bilgi sistemlerinin güvenliği hakkındaki 10 Ocak 2017 tarihli (EU, Euratom) 2017/46 sayılı Komisyon Kararı uyarınca gerçekleştirilir.</p>

            <p>Komisyon'un yüklenicileri, Komisyon adına verilerinizin herhangi bir işleme faaliyeti için özel bir sözleşme maddesiyle ve AB Üyesi Devletler'de Genel Veri Koruma Yönetmeliği'nin aktarımından gelen gizlilik yükümlülükleriyle sınırlandırılmıştır (‘GDPR’ Tüzük (EU) 2016/679).</p>

            <p>Komisyon kişisel verilerinizi korumak amacıyla belli sayıda mevcut teknik ve organizasyonel tedbirler almıştır. Teknik tedbirler, çevrim içi güvenliği, veri kaybı riskini, verilerin değiştirilmesini veya yetkisiz erişimi ele alan uygun eylemler içererek, işleme faaliyeti ve işlenmekte olan kişisel verilerin doğası gereği ortaya çıkan riski göz önünde bulundurur. Organizasyonel tedbirler, kişisel verilerin bu işleme faaliyetinin amaçları için meşru bir ihtiyaç ile birlikte yalnızca yetkili kişilerle sınırlandırılmasını içerir.</p>




            <h3><strong>7. Verilerinize kimler erişim sağlayabilir ve kimlere ifşa edilebilir?</strong></h3>

            <p>Kişisel verilerinize erişim, bu işleme faaliyetini gerçekleştirmekten sorumlu Komisyon personeline ve “bilmesi gereken” ilkesi uyarınca yetkili personele sağlanır. Bu personel, yasaya ve gerektiğinde ek gizlilik anlaşmalarına uyar.</p>

            <p>Açık bir şekilde, tarafınızdan sağlanan tüm kişisel veriler web sitelerinin yöneticilerinin (Komisyon personeli) yanı sıra bilmesi gereken temelinde diğer Komisyon personeli tarafından erişilebilir. . Dahası, kişisel bilgileriniz ve etkinlik bilgileri, yerel etkinlik organizasyonu veya takip için EU Code Week Elçileri ve Eğitim koordinatörü ağlarının üyeleriyle paylaşılacaktır.</p>

            <p>İrtibat noktalarının görünürlüğünü artırmak için, kişisel verileriniz herkese açık https://codeweek.eu web sitesinde herhangi bir erişim kısıtlaması olmaksızın yayımlanır.</p>

            <p><i>Verilerin üçüncü taraflara aktarımı ile ilgili</i></p>

            <p>Topladığımız veriler, kanunun mecbur tutabileceği amaçlar dışında, herhangi bir üçüncü tarafa verilmeyecektir.</p>




            <h3><strong>8. Haklarınız nelerdir ve bunları nasıl kullanabilirsiniz?</strong></h3>

            <p>(EU) 2018/1725 Tüzüğü Bölüm III altında (Madde 14-25) ‘veri sahibi’ olarak özel haklarınız vardır - özellikle erişim hakkı, kişisel verilerinizin düzenlenmesi veya silinmesi ve kişisel verilerinizin işlenmesini sınırlama hakkı. Uygulanabildiği yerlerde, işleme faaliyetine itiraz etme veya veri taşınabilirliği hakkınız da vardır.</p>
            <p>Mevcut işleme faaliyeti için bize kişisel verilerinizi sağlamaya izin verdiniz ve Veri Denetçisi'ni bilgilendirerek istediğiniz zaman izninizi geri çekebilirsiniz. Geri çekme işlemi, siz izni geri çekmeden önce gerçekleştirilen işleme faaliyetinin yasalara uygunluğunu etkilemez.</p>
            <p>Veri Denetçisi ile irtibata geçerek veya Veri Koruma Görevlisi ile anlaşmazlık olması durumunda haklarınızı kullanabilirsiniz. Gerekirse, Avrupa Veri Koruma Süpervizörü ile de iletişime geçebilirsiniz. Bu kişilerin irtibat bilgileri aşağıdaki 9. Başlık altında verilmiştir.</p>



