            <h1 style="margin-bottom:10px;">MBROJTJA E TË DHËNAVE TUAJA PERSONALE</h1>

            <h3><strong>1. Prezantimi</strong></h3>

            <p>Komisioni Evropian (më tej “Komisioni”) është i përkushtuar të mbrojë të dhënat tuaja personale dhe të respektojë privatësinë tuaj. Komisioni mbledh dhe përpunon më tej të dhënat personale në përputhje me Rregulloren (BE) 2018/1725 të Parlamentit Evropian dhe Këshillit të 23 tetorit 2018 për mbrojtjen e personave fizikë në lidhje me përpunimin e të dhënave personale nga institucionet, organet, zyrat dhe agjencitë e Bashkimit dhe mbi lëvizjen e lirë të këtyre të dhënave (Rregullorja shfuqizuese (KE) Nr. 45/2001).</p>

            <p>Kjo deklaratë e privatësisë shpjegon arsyen e mbledhjes dhe përpunimit, mënyrën e mbledhjes, trajtimit dhe sigurimit të mbrojtjes së të gjitha të dhënave personale të paraqitura, mënyrën e përdorimit të këtij informacioni dhe cilat të drejta mund të ushtroni në lidhje me të dhënat tuaja personale (e drejta për t’i parë, korrigjuar, bllokuar etj). Ajo gjithashtu specifikon detajet e kontaktit të Kontrolluesit përgjegjës të të Dhënave me të cilin mund të ushtroni të drejtat tuaja, Zyrtarin për Mbrojtjen e të Dhënave dhe Mbikëqyrësin Evropian për Mbrojtjen e të Dhënave.</p>

            <p>Kjo deklaratë e privatësisë ka të bëjë me mbledhjen dhe publikimin e të dhënave personale në faqen e internetit Codeweek.eu të arritshme publikisht, të personave që shërbejnë si pika kontakti për aktivitetet e EU Code Week (ambasadorët e Code Week, Koordinatorët e Ministrisë së Arsimit, mësuesit kryesorë, si dhe organizatorët e veprimtarive dhe ngjarjeve).</p>



            <h3><strong>2. Pse i përpunojmë të dhënat tuaja?</strong></h3>

            <p>Komisioni Evropian mbledh dhe boton informacionin tuaj personal për të lehtësuar identifikimin e pikave të kontaktit nga palët e interesuara ose qytetarët e interesuar. Ofrimi i personave realë si pika kontakti është mënyra më e mirë dhe më efikase për të siguruar që personat e interesuar të kontaktojnë me shërbimet e Komisionit.</p>

            <p>Të dhënat tuaja personale nuk do të përdoren për ndonjë vendimmarrje të automatizuar, përfshirë profilizimin.</p>




            <h3><strong>3. Mbi çfarë baze(a) ligjore i përpunojmë të dhënat tuaja personale</strong></h3>

            <p>Operacionet e përpunimit të të dhënave personale për publikimin e pikave të kontaktit janë të ligjshme sipas nenit 5(1)(d) të Rregullores (BE) 2018/1725 sepse ju dhatë pëlqimin tuaj për përpunimin e të dhënave tuaja personale përmes një formulari në internet ose kur ju ratë dakord që ne të përdorim emailin dhe emrin e përdoruesit nëse regjistroheni me një rrjet social.</p>



            <h3><strong>4. Cilat të dhëna personale mbledhim dhe përpunojmë më tej?</strong></h3>

            <p>Të dhënat personale të mbledhura janë informacione që e bëjnë identifikimin tuaj si pikë kontakti më të lehtë dhe rrisin shikueshmërinë tuaj për publikun, përkatësisht: titullin, emrin, mbiemrin, pozicionin, adresat zyrtare të postës elektronike dhe postare, numrin e telefonit, fotografinë, llogarinë e medias sociale, biografinë.</p>

            <p>Ju i keni dhënë këto të dhëna personale mbi baza vullnetare, duke plotësuar formularin e aplikimit.</p>

            <p>Sigurimi i të dhënave personale të caktuara është i detyrueshëm për publikimin e aktiviteteve dhe/ose informacionit të kontaktit në faqen e internetit codeweek.eu. Nëse nuk jepni të dhënat tuaja personale, aktiviteti juaj nuk do të publikohet dhe/ose nuk do të mund të merrni pjesë në rrjetet e lartpërmendura.</p>

            <p>Ju siguroni të gjitha të dhënat e tjera personale mbi baza vullnetare.</p>



            <h3><strong>5. Për sa kohë i mbajmë të dhënat tuaja personale?</strong></h3>

            <p>Komisioni mban të dhënat tuaja personale vetëm për kohën e nevojshme për të përmbushur qëllimin e mbledhjes ose përpunimit të mëtejshëm të përshkruar në pikën 2, përkatësisht për aq kohë sa ju shërbeni si një pikë kontakti.</p>

            <p>Të dhënat tuaja personale hiqen nga faqja e internetit me qasje publike menjëherë pasi të keni ndërprerë funksionin e pikës së kontaktit nëse nuk keni dhënë pëlqimin tuaj për ta përfshirë në bazën e të dhënave për aktivitete të ardhshme.</p>



            <h3><strong>6. Si i mbrojmë dhe ruajmë të dhënat tuaja personale?</strong></h3>

            <p>Të gjitha të dhënat personale në format elektronik (postë elektronike, dokumente, baza e të dhënave, grupe të dhënash të ngarkuara, etj) ruhen, ose në serverët e Komisionit Evropian, ose të kontraktuesit të tij. Të gjitha operacionet e përpunimit kryhen në bazë të Vendimit të Komisionit (BE, Euratom) 2017/46 të 10 janarit 2017 për sigurinë e sistemeve të komunikimit dhe informacionit në Komisionin Evropian.</p>

            <p>Kontraktuesit e Komisionit janë të detyruar nga një klauzolë specifike kontraktuale për çdo operacion të përpunimit të të dhënave tuaja në emër të Komisionit dhe nga detyrimet e konfidencialitetit që rrjedhin nga transpozimi i Rregullores së Përgjithshme të Mbrojtjes së të Dhënave në Shtetet Anëtare të BE-së (Rregullorja “GDPR” (BE) 2016/679).</p>

            <p>Për të mbrojtur të dhënat tuaja personale, Komisioni ka ngritur një numër masash teknike dhe organizative. Masat teknike përfshijnë veprime të përshtatshme për të trajtuar sigurinë në internet, rrezikun e humbjes së të dhënave, ndryshimin e të dhënave ose hyrjen e paautorizuar, duke marrë parasysh rrezikun e paraqitur nga përpunimi dhe natyrën e të dhënave personale që përpunohen. Masat organizative përfshijnë kufizimin e qasjes në të dhënat personale vetëm nga personat e autorizuar me një nevojë të ligjshme për të ditur për qëllimet e këtij operacioni përpunimi.</p>




            <h3><strong>7. Kush ka qasje në të dhënat tuaja dhe kujt i zbulohen?</strong></h3>

            <p>Qasja në të dhënat tuaja personale i ofrohet stafit të Komisionit përgjegjës për kryerjen e këtij operacioni përpunimi dhe personelit të autorizuar sipas parimit “nevojë për të ditur”. Ky personel i përmbahet marrëveshjeve ligjore dhe kur kërkohet, marrëveshjeve konfidenciale shtesë.</p>

            <p>Konkretisht, të gjitha të dhënat personale të paraqitura nga ju mund të arrihen nga administratorët e faqeve të internetit (stafi i Komisionit), si dhe nga personeli tjetër i Komisionit sipas nevojës për të ditur. . Për më tepër, informacioni personal juaj dhe informacioni për ngjarjet do të ndahen me anëtarët e ambasadorëve të EU Code Week dhe rrjetet e koordinatorëve arsimorë për organizimin e aktiviteteve lokale ose ndjekjen e tyre.</p>

            <p>Me qëllim të rritjes së shikueshmërisë së pikave të kontaktit, të dhënat tuaja personale publikohen pa kufizime të qasjes në faqen publike të internetit: https://codeweek.eu.</p>

            <p><i>Në lidhje me transferimin e të dhënave te palë të treta</i></p>

            <p>Informacioni që mbledhim nuk i jepet asnjë pale të tretë, përveç deri në masën dhe për qëllimin që mund të na kërkohet sipas ligjit.</p>




            <h3><strong>8. Cilat janë të drejtat tuaja dhe si mund t’i ushtroni ato?</strong></h3>

            <p>Ju keni të drejta specifike si “subjekt i të dhënave” sipas Kapitullit III (Nenet 14-25) të Rregullores (BE) 2018/1725, në veçanti të drejtën për të hyrë, korrigjuar ose fshirë të dhënat tuaja personale dhe të drejtën për të kufizuar përpunimin e të dhëna tuaja personale. Kur është e zbatueshme, ju gjithashtu keni të drejtë të kundërshtoni përpunimin ose të drejtën e bartjes së të dhënave.</p>
            <p>Ju jeni pajtuar që të na i jepni neve të dhënat tuaja personale për operacionin aktual të përpunimit dhe mund ta tërhiqni pëlqimin tuaj në çdo kohë duke njoftuar Kontrolluesin e të Dhënave. Tërheqja nuk do të ndikojë në ligjshmërinë e përpunimit të bërë para se të tërhiqni pëlqimin.</p>
            <p>Ju mund të ushtroni të drejtat tuaja duke kontaktuar Kontrolluesin e të Dhënave ose në rast konflikti Zyrtarin për Mbrojtjen e të Dhënave. Nëse është e nevojshme, mund t’i drejtoheni edhe Mbikëqyrësit Evropian për Mbrojtjen e të Dhënave. Informacioni i tyre i kontaktit jepet në Kapitullin 9 më poshtë.</p>



