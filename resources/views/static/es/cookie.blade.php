            <h1 style="margin-bottom:10px;">Política de <i>cookies</i></h1>

            <h3><strong>¿Qué son las <i>cookies</i>?</strong></h3>

            <p>
            <div>Una <i>cookie</i> es un archivo de texto pequeño que un sitio web almacena en su ordenador o dispositivo móvil cuando lo visita.</div>

            <div>
                <ul>
                    <li>Las <strong><i>cookies</i> propias</strong> son las <i>cookies</i> creadas por el sitio web que visita. Solo ese sitio web puede leerlas. Además, un sitio web podría utilizar servicios externos, que también crean sus propias <i>cookies</i>, llamadas <strong><i>cookies</i> de terceros</strong>.</li>
                    <li>Las <i>cookies</i> persistentes son las que se guardan en su ordenador y no se eliminan automáticamente al cerrar el navegador, a diferencia de una <i>cookie</i> de sesión, que se elimina al cerrar el navegador.</li>
                </ul>

            </div>

            <p>La primera vez que visite el sitio web de codeweek.eu, verá un mensaje en que se le solicitará que <strong>acepte o rechace las <i>cookies</i></strong>.</p>

            <p>Su objetivo es permitir que el sitio recuerde sus preferencias (como su nombre de usuario, su idioma, etc.) durante un período determinado.</p>

            <p>De esta forma, no tendrá que volver a introducir esta información cuando navegue por el sitio durante la misma visita.</p>

            <p>Asimismo, las <i>cookies</i> se pueden utilizar para establecer estadísticas anonimizadas relativas a la experiencia de navegación en nuestros sitios.</p>
            </p>


