            <div>Utilizamos <strong>tres tipos de <i>cookies</i> propias</strong> para:<ul>
                    <li>almacenar las preferencias del visitante,</li>
                    <li>hacer que funcionen los sitios web,</li>
                    <li>recopilar datos analíticos (sobre el comportamiento del usuario).</li>
                </ul>
