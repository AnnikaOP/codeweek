            <h1 style="margin-bottom:10px;">PROTECCIÓN DE SUS DATOS PERSONALES</h1>

            <h3><strong>1. Introducción</strong></h3>

            <p>La Comisión Europea (en «la Comisión») se compromete a proteger sus datos personales y respetar su intimidad. La Comisión recopila y trata los datos personales con arreglo al el Reglamento (UE) 2018/1725 del Parlamento Europeo y del Consejo, de 23 de octubre de 2018, relativo a la protección de las personas físicas en lo que respecta al tratamiento de datos personales por las instituciones, órganos y organismos de la Unión, y a la libre circulación de esos datos [por el que se deroga el Reglamento (CE) n. º45/2001].</p>

            <p>En la presente declaración de confidencialidad se explica el motivo de la recopilación y el tratamiento, la forma en que se recopilan, manejan y se garantiza la protección de todos los datos personales facilitados, la manera en que se utiliza dicha información y los derechos que puede ejercer con relación a sus datos personales (derecho de acceso, rectificación, bloqueo, etc.). Asimismo, se especifican los datos de contacto del responsable del tratamiento pertinente al que puede dirigirse para ejercer sus derechos, del delegado de protección de datos y del Supervisor Europeo de Protección de Datos.</p>

            <p>La presente declaración de confidencialidad se refiere a la recopilación y publicación de datos personales de personas que actúan como puntos de contacto para actividades de la Semana de la Programación de la Unión Europea (UE) —embajadores de la Semana de la Programación, coordinadores de los ministerios de educación y profesores líderes, así como organizadores de actividades y eventos— en el sitio web de acceso público codeweek.eu.</p>



            <h3><strong>2. ¿Por qué se tratan sus datos?</strong></h3>

            <p>La Comisión Europea recopila y publica sus datos personales para que las partes o los ciudadanos interesados puedan identificar fácilmente a los puntos de contacto. La forma más eficaz de garantizar que las personas interesadas pueden contactar con los servicios de la Comisión es ofrecer a personas reales como puntos de contacto.</p>

            <p>Sus datos personales no se utilizarán para decisiones automatizadas, incluida la elaboración de perfiles.</p>




            <h3><strong>3. ¿Sobre qué base(s) jurídica(s) se tratan sus datos personales?</strong></h3>

            <p>Las operaciones de tratamiento de datos personales para la publicación de puntos de contacto son legales en virtud del con el artículo 5, apartado 1, letra d) del Reglamento (UE) 2018/1725, ya que usted otorgó su consentimiento al tratamiento de sus datos personales a través de un formulario web o cuando aceptó que utilizáramos su correo electrónico y nombre de usuario, si se registró con una red social.</p>



            <h3><strong>4. ¿Qué datos personales se recopilan y tratan ulteriormente?</strong></h3>

            <p>Los datos personales recopilados son información que facilita su identificación como punto de contacto y que aumenta su visibilidad ante el público, a saber: título, nombre, apellidos, puesto, direcciones postal y de correo electrónico profesionales, número de teléfono, fotografía, cuenta en redes sociales y biografía.</p>

            <p>Usted facilitó estos datos personales de forma voluntaria al cumplimentar el formulario de registro.</p>

            <p>El suministro de determinados datos personales es obligatorio para que las actividades o la información de contacto se puedan publicar en el sitio web codeweek.eu. Si no facilita sus datos personales, su actividad no se publicará ni podrá participar en las redes mencionadas anteriormente.</p>

            <p>Todos los demás datos personales los proporciona de forma voluntaria.</p>



            <h3><strong>5. ¿Cuánto tiempo se conservan sus datos personales?</strong></h3>

            <p>La Comisión solo conserva sus datos personales el tiempo necesario su recopilación y tratamiento ulterior, tal como se describe en el punto 2, es decir, mientras actúe como punto de contacto.</p>

            <p>Sus datos personales serán eliminados del sitio web disponible públicamente en cuanto deje de actuar como punto de contacto, a menos que haya aceptado que se le incluya en la base de datos para futuras actividades.</p>



            <h3><strong>6. ¿Cómo se protegen y salvaguardan sus datos personales?</strong></h3>

            <p>Todos los datos personales en formato electrónico (correos electrónicos, documentos, bases de datos, lotes de datos cargados, etc.) se almacenan en los servidores de la Comisión Europea o de su contratista. Todas las operaciones de tratamiento se realizan con arreglo a la Decisión (UE, Euratom) 2017/46 de la Comisión, del 10 de enero de 2017, sobre la seguridad de los sistemas de información y comunicación de la Comisión Europea.</p>

            <p>Los contratistas de la Comisión están sujetos a una cláusula contractual específica para cualquier operación de tratamiento de sus datos en representación de la Comisión, así como por las obligaciones de confidencialidad que se derivan de la transposición del Reglamento General de Protección de Datos (RGPD) de los Estados miembros de la UE [Reglamento «RGPD» (UE) 2016/679].</p>

            <p>A fin de proteger sus datos personales, la Comisión ha establecido una serie de medidas técnicas y organizativas. Las medidas técnicas incluyen acciones apropiadas para hacer frente a la seguridad en línea, al riesgo de la pérdida de datos, a la modificación de datos o al acceso no autorizado, tomando en consideración el riesgo que presenta el tratamiento y la naturaleza de los datos personales que se tratan. Las medidas organizativas incluyen la restricción del acceso a los datos personales únicamente a las personas autorizadas con una necesidad legítima de conocerlos a efectos de esta operación de tratamiento.</p>




            <h3><strong>7. ¿Quién puede acceder a sus datos? ¿A quién se comunican?</strong></h3>

            <p>El acceso a sus datos personales se facilita al personal de la Comisión responsable de esta operación de tratamiento y al personal autorizado de conformidad con el principio de la «necesidad de conocer». Dicho personal está obligado a cumplir cualquier acuerdo legal y, cuando así se requiera, cualquier acuerdo de confidencialidad adicional.</p>

            <p>Concretamente, los administradores de los sitios web (personal de la Comisión), así como otro personal de la Comisión que necesiten conocerlos, podrán acceder a todos los datos personales que nos proporcione. . Además, sus datos personales y la información sobre el evento se compartirán con miembros de las redes de coordinadores educativos y con embajadores de la Semana de la Programación de la UE con el objetivo de organizar o hacer un seguimiento de las actividades locales.</p>

            <p>A fin de aumentar la visibilidad de los puntos de contacto, sus datos personales se publican sin ninguna restricción de acceso en el sitio web público: https://codeweek.eu.</p>

            <p><i>Con relación a la transferencia de datos a terceros</i></p>

            <p>La información que recopilemos no se enviará a terceros, excepto en la medida en que tengamos que hacerlo para respetar la legislación.</p>




            <h3><strong>8. ¿Cuáles son sus derechos y cómo puede ejercerlos?</strong></h3>

            <p>Tiene derechos específicos como «interesado» con arreglo al capítulo 3 (artículos 14 a 25) del Reglamento (UE) 2018/1725, en concreto, el derecho de acceso, rectificación o supresión de sus datos personales y el derecho a la limitación del tratamiento de sus datos personales. Cuando corresponda, también tiene derecho a oponerse al tratamiento o a la derecho a la portabilidad de los datos.</p>
            <p>Ha aceptado facilitarnos sus datos personales para la actual operación de tratamiento y puede retirar su consentimiento en cualquier momento mediante notificación al responsable del tratamiento. Dicha retirada no afectará a la legalidad del tratamiento realizado antes de haber retirado el consentimiento.</p>
            <p>Puede ejercer sus derechos poniéndose en contacto con el responsable del tratamiento o, en caso de conflicto, con el delegado de protección de datos. Si fuese necesario, también puede dirigirse al Supervisor Europeo de Protección de Datos. Sus datos de contacto se indican en el apartado 9 a continuación.</p>



