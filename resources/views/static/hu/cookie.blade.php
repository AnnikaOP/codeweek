            <h3><strong>Mik azok a cookie-k?</strong></h3>

            <p>
            <div>A cookie-k olyan kisméretű szövegfájlok, amelyeket a webhely tárol el az oldalaira látogató felhasználó számítógépén, illetve mobilkészülékén.</div>

            <div>
                <ul>
                    <li>A <strong>belső cookie-k</strong> (first-party cookies) azok, amelyeket az Ön által felkeresett honlap használ. Kizárólag az adott webhely tudja feldolgozni és hasznosítani ezeket. Valamely webhely emellett esetleg külső szolgáltatásokat is igénybe vehet, amelyek saját cookie-kat használnak, ezek az úgynevezett <strong>harmadik féltől származó cookie-k</strong> (third-party cookies).</li>
                    <li>Az állandó (persistent) cookie-k azok, amelyek – a munkameneti (session) cookie-kkal ellentétben – tartósan el vannak mentve számítógépén, tehát nem törlődnek automatikusan, amikor bezárja a böngészőprogramot.</li>
                </ul>

            </div>

            <p>A Codeweek weboldal első megtekintésekor Ön felkérést fog kapni a <strong>cookie-k elfogadására vagy elutasítására</strong>.</p>

            <p>Ez lehetővé teszi, hogy az oldal egy bizonyos ideig emlékezzen beállításaira (például felhasználónevére, a beállított nyelvre stb.).</p>

            <p>Ennek segítségével az oldalon történő böngészéskor egy adott látogatás során nem kell minden alkalommal újra megadnia adatait.</p>

            <p>A cookie-k a webhelyeinken történő böngészési szokásokkal kapcsolatos anonimizált statisztikai adatok gyűjtésére is szolgálnak.</p>
            </p>


