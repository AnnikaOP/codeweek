            <h1 style="margin-bottom:10px;">JŪSŲ ASMENS DUOMENŲ APSAUGA</h1>

            <h3><strong>1. Įvadas</strong></h3>

            <p>Europos Komisija (toliau – Komisija) yra įsipareigojusi apsaugoti jūsų asmens duomenis ir gerbti jūsų privatumą. Asmens duomenis Komisija renka ir toliau tvarko pagal 2018 m. spalio 23 d. Europos Parlamento ir Tarybos reglamentą (ES) 2018/1725 dėl fizinių asmenų apsaugos Sąjungos institucijoms, organams, tarnyboms ir agentūroms tvarkant asmens duomenis ir dėl laisvo tokių duomenų judėjimo (kuriuo panaikinamas Reglamentas (EB) Nr. 45/2001).</p>

            <p>Šiame privatumo pareiškime paaiškinta, kodėl renkame ir tvarkome jūsų asmens duomenis, kaip juos renkame, tvarkome, kaip užtikriname visų jūsų asmens duomenų apsaugą, kaip ši informacija naudojama ir kokiomis galite pasinaudoti su savo asmens duomenimis susijusiomis teisėmis (teise susipažinti, ištaisyti, užblokuoti ir kt.). Jame taip pat nurodyti atsakingo duomenų valdytojo, su kuriuo susisiekę galite pasinaudoti savo teisėmis, duomenų apsaugos pareigūno ir Europos duomenų apsaugos priežiūros pareigūno kontaktiniai duomenys.</p>

            <p>Šiame privatumo pareiškime aprašytas asmenų, kurie veikia kaip ES programavimo savaitės veiklos kontaktiniai asmenys (programavimo savaitės ambasadoriai, švietimo ministerijos koordinatoriai, vadovaujantys mokytojai ir veiklos bei renginių organizatoriai) asmens duomenų rinkimas ir skelbimas viešai prieinamoje svetainėje „codeweek.eu“.</p>



            <h3><strong>2. Kodėl tvarkome jūsų duomenis?</strong></h3>

            <p>Europos Komisija renka ir skelbia jūsų asmens duomenis, kad suinteresuotiesiems subjektams arba susidomėjusiems piliečiams būtų lengviau nustatyti kontaktinius asmenis. Kaip kontaktinius asmenis nurodžius tikrus asmenis, galima geriausiai ir veiksmingiausiai užtikrinti, kad susidomėję žmonės galėtų susisiekti su Komisijos tarnybomis.</p>

            <p>Jūsų asmens duomenys nebus naudojami jokiam automatizuotam sprendimų priėmimui, įskaitant profiliavimą.</p>




            <h3><strong>3. Kokiu teisiniu pagrindu (kokiais teisiniais pagrindais) tvarkome jūsų asmens duomenis?</strong></h3>

            <p>Asmens duomenų tvarkymo operacijos kontaktinių asmenų skelbimo tikslais yra teisėtos pagal Reglamento (ES) 2018/1725 5 straipsnio 1 dalies d punktą, nes davėte sutikimą tvarkyti jūsų asmens duomenis, pateikdami internetinę formą arba sutikdami su tuo, kad naudotume jūsų el. paštą ir naudotojo vardą, jei prisijungėte per socialinį tinklą.</p>



            <h3><strong>4. Kokius asmens duomenis renkame ir toliau tvarkome?</strong></h3>

            <p>Renkami asmens duomenys yra informacija, pagal kurią lengviau nustatyti jus, kaip kontaktinį asmenį, ir kuri didina jūsų matomumą visuomenei, t. y. titulas, pavardė, vardas, pareigos, darbiniai pašto ir el. pašto adresai, telefono numeris, nuotrauka, socialinės žiniasklaidos paskyra, biografija.</p>

            <p>Šiuos asmens duomenis teikiate savanoriškai, užpildydami prašymo formą.</p>

            <p>Pateikti tam tikrus asmens duomenis būtina, kad svetainėje „codeweek.eu“ būtų galima paskelbti veiklą ir (arba) kontaktinę informaciją. Jums nepateikus savo asmens duomenų, jūsų veikla nebūtų skelbiama ir (arba) negalėtume dalyvauti pirmiau nurodytuose tinkluose.</p>

            <p>Visus kitus asmens duomenis teikiate savanoriškai.</p>



            <h3><strong>5. Kiek laiko saugome jūsų asmens duomenis?</strong></h3>

            <p>Komisija jūsų asmens duomenis saugo tik tol, kol jų reikia 2 punkte aprašytam rinkimo ir tolesnio tvarkymo tikslui įgyvendinti, t. y. tol, kol veikiate kaip kontaktinis asmuo.</p>

            <p>Jūsų asmens duomenys iš viešai prieinamos svetainės bus pašalinti, kai nustosite veikti kaip kontaktinis asmuo, nebent davėte sutikimą įtraukti jus į duomenų bazę būsimai veiklai.</p>



            <h3><strong>6. Kaip saugome ir apsaugome jūsų asmens duomenis?</strong></h3>

            <p>Visi asmens duomenys elektroniniu formatu (el. laiškai, dokumentai, išsiųsti duomenų paketai ir pan.) saugomi Europos Komisijos arba jos rangovų serveriuose. Visos tvarkymo operacijos atliekamos pagal 2017 m. sausio 10 d. Komisijos sprendimą (ES, Euratomas) 2017/46 dėl Europos Komisijos ryšių ir informacinių sistemų saugumo.</p>

            <p>Komisijos rangovai privalo laikytis specialios sutartinės sąlygos, kuri taikoma bet kokioms Komisijos vardu atliekamoms jūsų duomenų tvarkymo operacijoms, ir konfidencialumo įsipareigojimų, nustatytų į ES valstybių narių teisę perkeltame Bendrajame duomenų apsaugos reglamente (BDAR, Reglamentas (ES) 2016/679).</p>

            <p>Siekdama apsaugoti jūsų asmens duomenis, Komisija taiko įvairias technines ir organizacines priemones. Technines priemones sudaro tinkami veiksmai, kuriais siekiama užtikrinti saugumą internete, apsisaugoti nuo duomenų praradimo rizikos, duomenų pakeitimo arba neteisėtos prieigos, atsižvelgiant į riziką, kylančią tvarkant duomenis, ir tvarkomų asmens duomenų pobūdį. Organizacines priemones sudaro prieigos prie asmens duomenų apribojimas, kad su šiais duomenimis galėtų susipažinti tik įgaliotieji asmenys, kuriems pagrįstai reikia juos žinoti atliekant šią tvarkymo operaciją.</p>




            <h3><strong>7. Kas gali susipažinti su jūsų asmens duomenimis ir kam jie atskleidžiami?</strong></h3>

            <p>Prieiga prie jūsų asmens duomenų suteikiama už šios tvarkymo operacijos atlikimą atsakingiems Komisijos darbuotojams ir įgaliotiesiems darbuotojams, vadovaujantis būtinybės žinoti principu. Šie darbuotojai privalo laikytis teisės aktais nustatytų ir, jei reikia, papildomų konfidencialumo susitarimų.</p>

            <p>Konkrečiai tariant, su visais jūsų pateiktais asmens duomenimis gali susipažinti svetainių administratoriai (Komisijos darbuotojai) ir kiti Komisijos darbuotojai, remiantis būtinybės žinoti principu.  Be to, jūsų asmens duomenimis ir renginių informacija bus dalijamasi su ES programavimo savaitės ambasadorių ir švietimo koordinatorių tinklų nariais, kad būtų galima organizuoti veiklą vietoje arba imtis tolesnių veiksmų.</p>

            <p>Kad kontaktiniai asmenys būtų matomesni, jūsų asmens duomenys be jokių prieigos apribojimų skelbiami viešoje svetainėje „https://codeweek.eu“.</p>

            <p><i>Apie duomenų perdavimą trečiosioms šalims</i></p>

            <p>Mūsų renkama informacija nebus perduodama jokiai trečiajai šaliai, išskyrus tokiu mastu ir tokiu tikslu, kuriais to iš mūsų gali būti reikalaujama pagal teisės aktus.</p>




            <h3><strong>8. Kokios yra jūsų teisės ir kaip jomis galite pasinaudoti?</strong></h3>

            <p>Pagal Reglamento (ES) 2018/1725 III skyrių (14–25 straipsnius) jūs, kaip duomenų subjektas, turite konkrečias teises, ypač teisę susipažinti su savo asmens duomenimis, juos ištaisyti arba ištrinti ir teisę apriboti savo asmens duomenų tvarkymą. Tinkamais atvejais taip pat turite teisę nesutikti su tvarkymu arba teisę į duomenų perkeliamumą.</p>
            <p>Jūs sutikote pateikti mums savo asmens duomenis šiai tvarkymo operacijai, bet savo sutikimą galite bet kada atšaukti, pranešdami apie tai duomenų valdytojui. Atšaukimas nedarys poveikio tvarkymo teisėtumui iki tol, kol atšaukėte sutikimą.</p>
            <p>Savo teisėmis galite pasinaudoti susisiekę su duomenų valdytoju arba konflikto atveju – su duomenų apsaugos pareigūnu. Prireikus taip pat galite kreiptis į Europos duomenų apsaugos priežiūros pareigūną. Jų kontaktinė informacija pateikta toliau 9 skirsnyje.</p>



