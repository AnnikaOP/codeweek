            <h3><strong>Kas yra slapukai?</strong></h3>

            <p>
            <div>Slapukas – tai nedidelis tekstinis failas, kurį jūsų kompiuteryje arba mobiliajame prietaise saugo svetainė, kai joje lankotės.</div>

            <div>
                <ul>
                    <li><strong>Lankomos svetainės slapukai</strong> – tai slapukai, kuriuos nustato jūsų lankoma svetainė. Juos gali perskaityti tik ši svetainė. Svetainė taip pat gali naudotis išorės paslaugomis, kurios taip pat nusistato slapukus, vadinamuosius <strong>svetimus slapukus</strong>.</li>
                    <li>Ilgalaikiai slapukai – tai jūsų kompiuteryje įrašyti slapukai, kurie nėra pašalinami automatiškai, kai išeinate iš naršyklės. Jie skiriasi nuo seanso slapukų, kurie, jums išėjus iš naršyklės, yra pašalinami.</li>
                </ul>

            </div>

            <p>Pirmąkart apsilankę svetainėje „Codeweek“, būsite paraginti <strong>priimti arba atsisakyti priimti slapukus</strong>.</p>

            <p>To reikia, kad svetainė tam tikrą laikotarpį galėtų prisiminti jūsų nuostatas (pvz., naudotojo vardą, kalbą ir kt.).</p>

            <p>Todėl naršant svetainę to paties seanso metu jums nereikės jų įvesti iš naujo.</p>

            <p>Slapukai taip pat gali būti naudojami nuasmenintai statistinei informacijai apie mūsų svetainių naršymą nustatyti.</p>
            </p>


