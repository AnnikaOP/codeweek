            <h1 style="margin-bottom:10px;">ZAŠTITA VAŠIH OSOBNIH PODATAKA</h1>

            <h3><strong>1. Uvod</strong></h3>

            <p>Europska komisija (dalje u tekstu „Komisija”) obvezuje se štititi vaše osobne podatke i poštovati vašu privatnost. Komisija prikuplja i dalje obrađuje osobne podatke u skladu s Uredbom (EU) 2018/1725 Europskog parlamenta i Vijeća od 23. listopada 2018. o zaštiti pojedinaca u vezi s obradom osobnih podataka u institucijama, tijelima, uredima i agencijama Unije i o slobodnom kretanju takvih podataka (te o stavljanju izvan snage Uredbe (EZ) br. 45/2001).</p>

            <p>U ovoj izjavi o zaštiti privatnosti objašnjavaju se razlozi za prikupljanje i obradu osobnih podataka, način njihova prikupljanja, postupanja s njima i osiguravanja zaštite svih osobnih podataka, kako se ti podatci upotrebljavaju te koja prava možete ostvariti kad je riječ o vašim osobnim podatcima (pravo na pristup, ispravljanje, blokiranje itd.). U njoj su navedeni i podatci za kontakt nadležnog voditelja obrade koji vam može pomoći da ostvarite svoja prava, službenika za zaštitu podataka i Europskog nadzornika za zaštitu podataka.</p>

            <p>Ova izjava o zaštiti privatnosti odnosi se na prikupljanje i objavu osobnih podataka na javno dostupnoj internetskoj stranici Codeweek.eu osoba koje služe kao kontaktne točke za aktivnosti Europskog tjedna programiranja (ambasadori tjedna programiranja, koordinatori iz ministarstva obrazovanja, nastavnici voditelji, kao i organizatori aktivnosti i događanja).</p>



            <h3><strong>2. Kako obrađujemo vaše podatke?</strong></h3>

            <p>Europska komisija prikuplja i objavljuje vaše osobne podatke kako bi dionicima ili zainteresiranim građanima olakšala identifikaciju kontaktnih točaka. Pružanje stvarnih osoba kao kontaktnih točaka najbolji je i najučinkovitiji način da se osigura da zainteresirane osobe mogu doći u kontakt sa servisima Komisije.</p>

            <p>Vaši osobni podatci neće se upotrebljavati za automatizirano donošenje odluka, uključujući profiliranje.</p>




            <h3><strong>3. Na kojim pravnim osnovama obrađujemo vaše osobne podatke</strong></h3>

            <p>Postupci obrade osobnih podataka za objavu kontaktnih točaka zakoniti su prema članku 5. stavku 1. točki (d) Uredbe (EU) 2018/1725 zato što ste dali privolu za obradu svojih osobnih podataka putem internetskog obrasca ili onda kada ste pristali na to da upotrebljavamo vašu adresu e-pošte ili korisničko ime ako ste se prijavili putem društvene mreže.</p>



            <h3><strong>4. Koje osobne podatke prikupljamo i dalje obrađujemo?</strong></h3>

            <p>Prikupljeni osobni podatci jesu informacije s pomoću kojih se olakšava identifikacija vas kao kontaktne točke i s pomoću kojih se povećava vaša vidljivost za javnost, odnosno: titula, ime, prezime, položaj, profesionalne poštanske adrese i adrese e-pošte, broj telefona, slika, račun na društvenim mrežama, biografija.</p>

            <p>Te ste podatke pružili dobrovoljno ispunjavanjem obrasca za prijavu.</p>

            <p>Navođenje određenih osobnih podataka obavezno je za objavu aktivnosti i/ili podataka za kontakt na internetskoj stranici codeweek.eu. Ako ne navedete osobne podatke, vaša aktivnost neće biti objavljena i/ili nećete moći sudjelovati u prethodno navedenim mrežama.</p>

            <p>Sve druge osobne podatke pružate dobrovoljno.</p>



            <h3><strong>5. Koliko dugo čuvamo vaše osobne podatke?</strong></h3>

            <p>Komisija čuva vaše osobne podatke samo onoliko dugo koliko je potrebno za prikupljanje ili daljnju obradu koji su opisani u drugoj točki, odnosno onoliko dugo koliko djelujete kao kontaktna točka.</p>

            <p>Vaši će se osobni podatci ukloniti s javno dostupne internetske stranice čim završite svoju funkciju kontaktne točke, osim ako niste dali svoj pristanak da vas se uključi u bazu podataka za buduće aktivnosti.</p>



            <h3><strong>6. Kako štitimo vaše osobne podatke?</strong></h3>

            <p>Svi osobni podatci u elektroničkom obliku (e-pošta, dokumenti, baze podataka, učitane serije podataka itd.) pohranjeni su ili na poslužiteljima Europske komisije ili poslužiteljima njezina ugovaratelja. Svaka se obrada vrši u skladu s Odlukom Komisije (EU, Euratom) 2017/46 оd 10. siječnja 2017. o sigurnosti komunikacijskih i informacijskih sustava u Europskoj komisiji.</p>

            <p>Ugovaratelji Komisije obvezani su posebnom ugovornom klauzulom za sve aktivnosti obrade vaših podataka uime Komisije, kao i obvezama o povjerljivosti koje proizlaze iz prenošenja Opće uredbe o zaštiti podataka u državama članicama EU-a (Uredba OUZP (EU) 2016/679).</p>

            <p>Komisija je uvela niz tehničkih i organizacijskih mjera radi zaštite vaših osobnih podataka. Tehničke mjere uključuju odgovarajuće aktivnosti za osiguranje sigurnosti na internetu, ublažavanje rizika od gubitka podataka, sprječavanje izmjene podataka ili neovlaštenog pristupa podatcima, uzimajući u obzir rizik pri obradi i prirodu osobnih podataka koji se obrađuju. Organizacijske mjere uključuju ograničavanje pristupa osobnim podatcima isključivo na ovlaštene osobe kojima su ti podatci opravdano potrebni u svrhe predmetne obrade.</p>




            <h3><strong>7. Tko ima pristup vašim podatcima i kome se oni otkrivaju?</strong></h3>

            <p>Pristup vašim osobnim podatcima ima osoblje Komisije koje je odgovorno za obradu i ovlašteno osoblje prema načelu nužnog pristupa. To se osoblje mora pridržavati zakonskih odredaba, i kad je potrebno, odredaba dodatnih ugovora o povjerljivosti.</p>

            <p>Konkretno, svim osobnim podatcima koje pružite mogu pristupiti administratori internetske stranice (osoblje Komisije) kao i drugo osoblje Komisije prema načelu nužnog pristupa. . Nadalje, vaši osobni podatci i podatci o događaju dijelit će se s članovima mreža ambasadora Europskog tjedna programiranja i koordinatora iz područja obrazovanja radi organiziranja lokalnih aktivnosti ili daljnjih mjera.</p>

            <p>U svrhu povećane vidljivosti kontaktnih točaka, vaši osobni podatci objavljuju se bez ograničenja pristupa na javnoj internetskoj stranici: http://codeweek.eu.</p>

            <p><i>Prijenos podataka trećim stranama</i></p>

            <p>Podatci koje prikupimo neće se dati trećim stranama, osim u mjeri i u svrhu u kojoj nam zakon tako nalaže.</p>




            <h3><strong>8. Koja su vaša prava i kako ih možete ostvariti?</strong></h3>

            <p>Vaša posebna prava kao „ispitanika” u skladu s poglavljem III. (članci 14. – 25.) Uredbe (EU) 2018/1725 uključuju pravo na pristup vašim osobnim podatcima, njihov ispravak ili brisanje i pravo na ograničenje obrade vaših osobnih podataka. Ako je primjenjivo, imate i pravo prigovora na obradu ili pravo na prenosivost podataka.</p>
            <p>Pristali ste dati svoje osobne podatke za predmetnu obradu i taj pristanak možete povući u bilo kojem trenutku slanjem obavijesti voditelju obrade podataka. Povlačenje neće utjecati na zakonitost obrade provedene prije povlačenja vašeg pristanka.</p>
            <p>Svoja prava možete ostvariti tako što ćete se obratiti voditelju obrade podataka, a u slučaju spora službeniku za zaštitu podataka. Ako je potrebno, možete se obratiti i Europskom nadzorniku za zaštitu podataka. Njihovi su podatci za kontakt navedeni pod naslovom 9 u nastavku.</p>



