            <h1 style="margin-bottom:10px;">OCHRONA PAŃSTWA DANYCH OSOBOWYCH</h1>

            <h3><strong>1. Wprowadzenie</strong></h3>

            <p>Komisja Europejska (zwana dalej „Komisją”) jest zobowiązana do ochrony Państwa danych osobowych i poszanowania Państwa prywatności. Komisja gromadzi i przetwarza dane osobowe zgodnie z rozporządzeniem (UE) 2018/1725 Parlamentu Europejskiego i Rady z dnia 23 października 2018 roku w sprawie ochrony osób fizycznych w związku z przetwarzaniem danych osobowych przez instytucje, organy i jednostki organizacyjne Unii i swobodnego przepływu takich danych oraz uchylenia rozporządzenia (WE) nr 45/2001.</p>

            <p>Niniejsze oświadczenie o ochronie prywatności wyjaśnia powód gromadzenia i przetwarzania danych, sposoby, w jakie gromadzimy i przetwarzamy wszystkie udostępniane przez Państwa dane osobowe, a także zapewniamy ich ochronę oraz w jaki sposób informacje te są wykorzystywane i z jakich praw mogą Państwo korzystać w odniesieniu do swoich danych osobowych (prawo dostępu, sprostowania, zablokowania itp.). Oświadczenie określa również dane kontaktowe właściwego administratora danych, który pomoże Państwu w egzekwowaniu należnych praw, inspektora ochrony danych oraz Europejskiego Inspektora Ochrony Danych.</p>

            <p>Niniejsze oświadczenie o ochronie prywatności dotyczy gromadzenia i publikowania danych osobowych osób za pośrednictwem publicznie dostępnej strony internetowej Codeweek.eu. Są to dane kontaktowe osób związanych z działaniami prowadzonymi w ramach Europejskiego Tygodnia Kodowania (ambasadorów Tygodnia Kodowania, koordynatorów Ministerstwa Edukacji, Nauczycieli Prowadzących oraz organizatorów działań i wydarzeń).</p>



            <h3><strong>2. Dlaczego przetwarzamy Państwa dane?</strong></h3>

            <p>Komisja Europejska gromadzi i publikuje Państwa dane osobowe w celu ułatwienia identyfikacji osób kontaktowych przez zainteresowane strony lub obywateli. Udostępnianie danych prawdziwych osób jako osób kontaktowych jest najlepszym i najskuteczniejszym sposobem zapewnienia zainteresowanym stronom możliwości kontaktu ze służbami Komisji.</p>

            <p>Państwa dane osobowe nie będą wykorzystywane w systemach zautomatyzowanego podejmowania decyzji, w tym w celu profilowania.</p>




            <h3><strong>3. Na jakiej podstawie prawnej przetwarzane są Państwa dane osobowe?</strong></h3>

            <p>Przetwarzanie danych osobowych na potrzeby publikacji danych osób kontaktowych jest zgodne z prawem na mocy art. 5 ust 1 lit d) rozporządzenia (UE) 2018/1725, ponieważ wyrazili Państwo zgodę na przetwarzanie Państwa danych osobowych za pośrednictwem formularza internetowego lub wyrazili Państwo zgodę na przetwarzanie przez nas Państwa adresu e-mail i nazwy użytkownika, jeśli zapisali się Państwo za pośrednictwem sieci społecznościowej.</p>



            <h3><strong>4. Jakie dane osobowe gromadzimy i przetwarzamy?</strong></h3>

            <p>Zebrane dane osobowe to informacje umożliwiające identyfikację osoby kontaktowej i zwiększające jej widoczność wśród użytkowników, obejmują: tytuł, imię, nazwisko, stanowisko, służbowe adresy pocztowe i e-mail, numer telefonu, zdjęcie, konto w mediach społecznościowych, a także biogram.</p>

            <p>Podali Państwo te dane osobowe dobrowolnie, podczas wypełniania formularza zgłoszeniowego.</p>

            <p>Podanie niektórych danych osobowych jest obowiązkowe w celu publikacji działań lub danych kontaktowych na stronie internetowej codeweek.eu. Jeśli nie podadzą Państwo swoich danych osobowych, Państwa działanie nie zostanie opublikowane lub nie będą Państwo mieli możliwości uczestniczyć w wyżej wymienionych społecznościach.</p>

            <p>Wszystkie inne dane osobowe podają Państwo dobrowolnie.</p>



            <h3><strong>5. Jak długo przechowujemy Państwa dane osobowe?</strong></h3>

            <p>Komisja przechowuje Państwa dane osobowe jedynie przez okres niezbędny do realizacji celu gromadzenia lub dalszego przetwarzania danych, o którym mowa w punkcie 2, czyli tak długo, jak długo będą Państwo pełnić rolę osoby kontaktowej.</p>

            <p>Państwa dane osobowe zostaną usunięte z publicznie dostępnej strony internetowej z chwilą, gdy zdecydują się Państwo o zaprzestaniu pełnienia roli osoby kontaktowej, chyba że wyrazili Państwo zgodę na umieszczenie ich w bazie danych na potrzeby przyszłych działań.</p>



            <h3><strong>6. W jaki sposób chronimy i zabezpieczamy Państwa dane osobowe?</strong></h3>

            <p>Wszystkie dane osobowe w formacie elektronicznym (wiadomości e-mail, dokumenty, bazy danych, przesłane partie danych itp.) są przechowywane na serwerach Komisji Europejskiej lub jej usługodawcy. Wszystkie operacje przetwarzania są przeprowadzane zgodnie z decyzją Komisji (UE, Euratom) 2017/46 z dnia 10 stycznia 2017 roku w sprawie bezpieczeństwa systemów teleinformatycznych w Komisji Europejskiej.</p>

            <p>Usługodawcy Komisji są zobowiązani szczególną klauzulą umowną dotycząca wszelkich operacji przetwarzania Państwa danych w imieniu Komisji oraz objęci obowiązkami w zakresie poufności wynikającymi z transpozycji ogólnego rozporządzenia o ochronie danych w państwach członkowskich UE (Rozporządzenie "RODO"(UE) 2016/679).</p>

            <p>Komisja wprowadziła szereg środków technicznych i organizacyjnych w celu ochrony Państwa danych osobowych. Środki techniczne obejmują odpowiednie działania w zakresie bezpieczeństwa online, ryzyka utraty danych, zmiany danych lub nieuprawnionego dostępu, z uwzględnieniem ryzyka związanego z przetwarzaniem i charakteru przetwarzanych danych osobowych. Środki organizacyjne obejmują ograniczenie dostępu do danych osobowych wyłącznie do osób upoważnionych, które wymagają dostępu do danych ze względu na uzasadnione potrzeby związane z ich przetwarzaniem.</p>




            <h3><strong>7. Kto ma dostęp do Państwa danych i komu są one ujawniane?</strong></h3>

            <p>Dostęp do Państwa danych osobowych mają pracownicy Komisji odpowiedzialni za ich przetwarzanie oraz upoważnieni pracownicy zgodnie z zasadą ograniczonego dostępu. Pracownicy ci podlegają przepisom ustawowym, a w razie potrzeby także dodatkowym umowom o zachowaniu poufności.</p>

            <p>Oznacza to, że wszystkie dane osobowe dostarczone przez Państwa mogą być udostępnione administratorom stron internetowych (pracownikom Komisji), jak również innym pracownikom Komisji na zasadzie ograniczonego dostępu. . Ponadto Państwa dane osobowe i informacje o wydarzeniach będą udostępniane członkom społeczności ambasadorów Europejskiego Tygodnia Kodowania i sieci koordynatorów edukacyjnych na potrzeby organizacji lokalnych działań lub działań następczych.</p>

            <p>W celu zwiększenia widoczności osób kontaktowych, Państwa dane osobowe są publikowane bez żadnych ograniczeń dostępu na publicznej stronie internetowej https://codeweek.eu.</p>

            <p><i>W odniesieniu do przekazywania danych podmiotom zewnętrznym</i></p>

            <p>Dane, które gromadzimy, nie będą przekazywane podmiotom zewnętrznym, chyba że w zakresie i w celu, w jakim jest to wymagane przez prawo.</p>




            <h3><strong>8. Jakie są Państwa prawa i jak mogą Państwo z nich korzystać?</strong></h3>

            <p>Posiadają Państwo szczególne prawa jako „osoba, której dane dotyczą” na mocy rozdziału III (art. 14–25) rozporządzenia (UE) 2018/1725, w szczególności prawo dostępu do danych osobowych, ich aktualizacji lub usuwania oraz prawo do ograniczenia przetwarzania danych osobowych. W stosownych przypadkach mają Państwo również prawo do sprzeciwu wobec przetwarzania lub prawo do przenoszenia danych.</p>
            <p>Zgodzili się Państwo na przekazanie nam swoich danych osobowych na potrzeby obecnej operacji przetwarzania danych i mogą Państwo wycofać swoją zgodę w dowolnym momencie, powiadamiając o tym administratora danych. Wycofanie zgody nie wpłynie na legalność przetwarzania danych przed wycofaniem zgody.</p>
            <p>Mogą Państwo korzystać ze swoich praw przez skontaktowanie się z administratorem Państwa danych lub, w przypadku sporów, także z inspektorem ochrony danych. W razie potrzeby mogą Państwo również zwrócić się do Europejskiego Inspektora Ochrony Danych. Stosowne dane kontaktowe znajdują się poniżej, w rozdziale 9.</p>



