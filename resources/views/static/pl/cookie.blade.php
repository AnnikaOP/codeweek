            <h1 style="margin-bottom:10px;">Polityka dotycząca plików cookie</h1>

            <h3><strong>Czym są pliki cookie?</strong></h3>

            <p>
            <div>Plik cookie to mały plik tekstowy, który strona internetowa przechowuje na komputerze lub urządzeniu przenośnym użytkownika odwiedzającego stronę internetową.</div>

            <div>
                <ul>
                    <li><strong>Pliki cookie administratora</strong>  to pliki należące do odwiedzanej strony internetowej. Tylko ta strona internetowa może je odczytać. Strona internetowa może ewentualnie korzystać z serwisów zewnętrznych, które również posiadają własne pliki cookie – <strong>są to pliki cookie osób trzecich</strong>.</li>
                    <li>Trwałe pliki cookie są zapisywane na komputerze użytkownika i nie są automatycznie usuwane po zamknięciu przeglądarki w przeciwieństwie do sesyjnych plików cookie, które są usuwane po zamknięciu przeglądarki.</li>
                </ul>

            </div>

            <p>Przy pierwszej wizycie na stronie internetowej Tygodnia Kodowania, zostaną Państwo poproszeni o <strong>zaakceptowanie lub odrzucenie plików cookie</strong>.</p>

            <p>Celem jest umożliwienie witrynie zapamiętania Państwa preferencji (takich jak nazwa użytkownika, język itp.) na pewien czas.</p>

            <p>Dzięki temu nie trzeba ponownie wprowadzać tych samych danych podczas jednych odwiedzin na stronie.</p>

            <p>Pliki cookie mogą być również używane do sporządzania anonimowych statystyk o korzystaniu z stron internetowych.</p>
            </p>


