                <h2>Wat is de EU-programmeerweek?</h2>
                <p>De EU-programmeerweek is een burgerinitiatief dat door vrijwilligers georganiseerd wordt met steun van de Europese Commissie. Iedereen - scholen, leerkrachten, bibliotheken, programmeerclubs, bedrijven en overheidsinstellingen - kan een #CodeWeek-activiteit organiseren en dit op de kaart van <a href="/events">codeweek.eu</a> zetten.</p>


                <h2>Wat heb je nodig om een activiteit te organiseren?</h2>
                <ul>
                    <li><strong>Een groep mensen die graag willen leren.</strong> Bijvoorbeeld je vrienden, kinderen, tieners, volwassen collega&rsquo;s, kennissen, ouders of grootouders. Vergeet niet dat twee mensen samen al een groep vormen!</li>
                    <li><strong>Leerkrachten of trainers</strong> die bekend zijn programmeren en die weten hoe ze anderen moeten onderwijzen en inspireren. Het aantal hangt af van het soort evenement en de omvang ervan.</li>
                    <li><strong>Een plek om te leren.</strong> Klaslokalen, bibliotheken, vergaderzalen en verschillende openbare ruimten zijn allemaal fantastische locaties voor evenementen.</li>
                    <li><strong>Computers en een internetverbinding.</strong> Afhankelijk van je doelgroep kan deelnemers gevraagd worden een eigen laptop mee te nemen.</li>

                    <li><strong>Programmeren zonder snoeren.</strong> Je hebt niet per se computers en een internetverbinding nodig om mensen als een computer te leren denken. Kijk maar eens naar onze <a href="/training/coding-without-computers">Unplugged leerbits</a> en ga aan de slag.</li>


                    <li><strong>Lesmateriaal.</strong> Laat deelnemers zien hoe leuk het kan zijn om zelf iets nieuws te maken. Bekijk onze <a
                                href="/resources">pagina met bronnen</a> en <a href="/training">leerbits</a> met video tutorials en lesplannen en pas ze aan aan de behoeften van je groep.</li>


                    <li><strong>Een methode om deelnemers te registreren.</strong> Als je maar beperkte ruimte tot je beschikking hebt, gebruik dan online tools zoals <a href="https://docs.google.com/forms/">Google Formulieren</a> en <a
                                href="https://www.eventbrite.com/">Eventbrite</a> om deelnemers te registreren.</li>
                    <li><a href="/add">Zet je evenement</a> ook op de <a href="/events">kaart van de programmeerweek</a>!</li>
                </ul>


                <h2>Hoe organiseer je een activiteit?</h2>
                <ul>
                    <li>Hoe je programmeerevenement eruit ziet, is helemaal aan jou. We adviseren je wel om wat <strong>praktische oefentijd</strong> in te plannen, waarin deelnemers zelf iets kunnen maken of wat kunnen experimenteren met hardware.</li>
                    <li>Maak gebruik van <strong>tools en technologie&euml;n</strong> die geschikt zijn voor je doelgroep. We adviseren gebruik te maken van <a href="http://codeweek.eu/resources/">open source materiaal dat vrij beschikbaar</a> is.</li>

                    <li>Zorg dat deelnemers aan het eind van je evenement aan elkaar <strong>laten zien</strong> wat ze hebben gemaakt.</li>

