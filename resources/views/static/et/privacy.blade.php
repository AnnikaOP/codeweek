            <h1 style="margin-bottom:10px;">TEIE ISIKUANDMETE KAITSE</h1>

            <h3><strong>1. Sissejuhatus</strong></h3>

            <p>Euroopa Komisjon (edaspidi „komisjon“) kaitseb isikuandmeid ja austab eraelu puutumatust. Komisjon lähtub isikuandmete kogumisel ja töötlemisel Euroopa Parlamendi ja nõukogu 23. oktoobri 2018. aasta määrusest (EL) 2018/1725, mis käsitleb füüsiliste isikute kaitset isikuandmete töötlemisel liidu institutsioonides, organites ja asutustes ning selliste andmete vaba liikumist (millega tunnistatakse kehtetuks määrus (EÜ) nr 45/2001).</p>

            <p>Käesolevates isikuandmete kaitse põhimõtetes selgitatakse, miks isikuandmeid kogutakse ja töödeldakse, kuidas neid kogutakse, kuidas kõiki esitatud isikuandmeid käideldakse ja kaitstakse, kuidas seda teavet kasutatakse ning millised õigused on teil seoses oma isikuandmetega (õigus pääseda andmetele ligi, neid parandada, blokeerida jne). Isikuandmete kaitse põhimõtted sisaldavad ka selle vastutava töötleja kontaktandmeid, kelle poole saate oma õiguste kasutamiseks pöörduda, samuti andmekaitseametniku ja Euroopa Andmekaitseinspektori kontaktandmeid.</p>

            <p>Käesolevad isikuandmete kaitse põhimõtted puudutavad ELi programmeerimisnädala Code Week tegevuste kontaktpunktidena tegutsevate isikute (Code Weeki saadikute, haridusministeeriumi koordinaatorite, juhtivate õpetajate, samuti tegevuste ja ürituste korraldajate) isikuandmete kogumist ja avaldamist avalikul veebisaidil Codeweek.eu.</p>



            <h3><strong>2. Miks teie isikuandmeid töödeldakse?</strong></h3>

            <p>Euroopa Komisjon kogub ja avaldab teie isikuandmeid, et sidusrühmadel ja huvitatud kodanikel oleks lihtsam kontaktisikuid tuvastada. Reaalsete isikute kasutamine kontaktpunktidena võimaldab kõige paremini ja tõhusamalt tagada, et huvitatud inimesed saavad komisjoni talitustega ühendust võtta.</p>

            <p>Teie isikuandmeid ei kasutata automatiseeritud otsuste tegemiseks, sh profiilianalüüsiks.</p>




            <h3><strong>3. Milline on teie isikuandmete töötlemise õiguslik alus?</strong></h3>

            <p>Isikuandmete töötlemine kontaktpunktide avaldamiseks on määruse (EL) 2018/1725 artikli 5 lõike 1 punkti d kohaselt seaduslik, kuna andsite nõusoleku oma isikuandmete töötlemiseks veebivormi kaudu või siis, kui suhtlusvõrgustiku kaudu sisse logides nõustusite, et kasutame teie e-posti aadressi ja kasutajanime.</p>



            <h3><strong>4. Milliseid isikuandmeid kogutakse ja töödeldakse?</strong></h3>

            <p>Kogutakse selliseid isikuandmeid, mis lihtsustavad teie kui kontaktisiku tuvastamist ja muudavad teid avalikkusele paremini nähtavaks: tiitel, eesnimi, perekonnanimi, ametikoht, ametialane posti- ja e-posti aadress, telefoninumber, pilt, suhtlusmeedia konto, elulookirjeldus.</p>

            <p>Esitasite need isikuandmed vabatahtlikult, täites avalduse vormi.</p>

            <p>Teatud isikuandmete esitamine on kohustuslik tegevuste ja/või kontaktteabe avaldamiseks veebisaidil codeweek.eu. Kui te ei esita oma isikuandmeid, siis teie tegevust ei avaldata ja/või te ei saa eespool nimetatud võrkudes osaleda.</p>

            <p>Te esitate kõik muud isikuandmed vabatahtlikult.</p>



            <h3><strong>5. Kui kaua me teie isikuandmeid säilitame?</strong></h3>

            <p>Komisjon säilitab teie isikuandmeid ainult nii kaua, kui see on vajalik punktis 2 kirjeldatud andmete kogumise eesmärgi täitmiseks või edasiseks töötlemiseks, nimelt senikaua, kuni te tegutsete kontaktisikuna.</p>

            <p>Teie isikuandmed eemaldatakse avalikult veebisaidilt niipea, kui te enam kontaktisikuna ei tegutse, välja arvatud juhul, kui olete andnud nõusoleku lisada end andmebaasi seoses edasise tegevusega.</p>



            <h3><strong>6. Kuidas me teie isikuandmeid kaitseme ja hoiame?</strong></h3>

            <p>Kõiki elektroonilises vormis olevaid isikuandmeid (e-kirjad, dokumendid, andmebaasid, üles laaditud andmehulgad jms) hoitakse kas Euroopa Komisjoni või tema töövõtja serverites. Kõik töötlemistoimingud tehakse vastavalt komisjoni 10. jaanuari 2017. aasta otsusele (EL, Euratom) 2017/46 Euroopa Komisjoni side- ja infosüsteemide turvalisuse kohta.</p>

            <p>Komisjoni töövõtjad peavad järgima lepingu eritingimust, mis puudutab isikuandmete töötlemist komisjoni nimel, ning konfidentsiaalsuskohustust, mis tuleneb isikuandmete kaitse üldmääruse (määrus (EL) 2016/679) ülevõtmisest ELi liikmesriikides.</p>

            <p>Teie isikuandmete kaitseks on komisjon rakendanud mitmeid tehnilisi ja korralduslikke meetmeid. Tehniliste meetmetega käsitletakse võrguturvalisuse kaitset, andmete kaotsimineku ohtu ning andmete muutmist või loata juurdepääsu andmetele, võttes arvesse töötlemisest ja töödeldavate andmete laadist tulenevat riski. Korralduslike meetmetega antakse juurdepääs isikuandmetele ainult volitatud isikutele, kellel on seaduslik vajadus neid teada kõnealuse andmetöötlustoimingu läbiviimiseks.</p>




            <h3><strong>7. Kellel on juurdepääs teie isikuandmetele ja kellele need avaldatakse?</strong></h3>

            <p>Teie isikuandmetele pääsevad ligi komisjoni töötajad, kes vastutavad andmete töötlemise eest, ja volitatud töötajad üksnes teadmisvajaduse põhimõtte kohaselt. Asjaomased töötajad peavad kinni pidama seadusjärgsetest ja vajaduse korral ka täiendavatest konfidentsiaalsuskokkulepetest.</p>

            <p>Konkreetselt pääsevad kõigile teie esitatud isikuandmetele ligi veebisaitide administraatorid (komisjoni töötajad) ja muud komisjoni töötajad teadmisvajaduse põhimõtte kohaselt.   Lisaks jagatakse teie isikuandmeid ja teavet ürituste kohta ELi programmeerimisnädala saadikute ja hariduskoordinaatorite võrgustike liikmetega kohaliku tegevuse korraldamiseks või järelmeetmete võtmiseks.</p>

            <p>Kontaktisikute nähtavuse suurendamiseks avaldatakse teie isikuandmed juurdepääsupiiranguteta avalikul veebisaidil https://codeweek.eu.</p>

            <p><i>Isikuandmete edastamine kolmandatele isikutele</i></p>

            <p>Kogutud teavet ei anta ühelegi kolmandale isikule, välja arvatud juhul, kui seda võidakse seadusega nõuda.</p>




            <h3><strong>8. Millised on teie õigused ja kuidas neid kasutada?</strong></h3>

            <p>Määruse (EL) 2018/1725 III peatüki (artiklite 14–25) kohaselt on teil andmesubjektina konkreetsed õigused, eelkõige õigus oma isikuandmetega tutvuda, neid parandada ja kustutada ning õigus piirata oma isikuandmete töötlemist. Teatavatel juhtudel on teil ka õigus andmete töötlemine vaidlustada ja lasta andmeid ühest süsteemist teise üle kanda.</p>
            <p>Olete olnud nõus esitama meile oma isikuandmed kõnealuseks töötlemiseks ja võite oma nõusoleku alati tagasi võtta, teatades vastutavale töötlejale sellest soovist. Nõusoleku tagasivõtmine ei mõjuta enne seda toimunud töötlemise õiguspärasust.</p>
            <p>Oma õiguste rakendamiseks pöörduge vastutava töötleja poole või vastuolude korral andmekaitseametniku poole. Vajaduse korral võite pöörduda ka Euroopa Andmekaitseinspektori poole. Kontaktandmed on esitatud allpool punktis 9.</p>



