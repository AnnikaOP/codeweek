            <h1 style="margin-bottom:10px;">ZAŠTITA LIČNIH PODATAKA</h1>

            <h3><strong>1. Uvod</strong></h3>

            <p>Evropska komisija (u daljem tekstu „Komisija“) je posvećena zaštiti vaših ličnih podataka i poštovanju vaše privatnosti. Komisija prikuplja i dalje obrađuje lične podatke u skladu sa Uredbom (EU) 2018/1725 Evropskog parlamenta i Saveta od 23. oktobra 2018. godine o zaštiti fizičkih lica po pitanju obrade ličnih podataka od strane institucija, tela, kancelarija i agencija Unije i o slobodnom kretanju tih podataka (ukidajući Uredbu (EK) br. 45/2001).</p>

            <p>Ova izjava o privatnosti objašnjava razlog za prikupljanje i obradu, način na koji prikupljamo, obrađujemo i osiguravamo zaštitu svih navedenih ličnih podataka, kako se te informacije koriste i koja prava možete da primenjujete u vezi sa vašim ličnim podacima (pravo pristupa, ispravke, blokade itd.). Takođe navodi detalje o kontaktu odgovornog kontrolora podataka sa kojim možete da primenite ova prava, službenika za zaštitu podataka i evropskog supervizora za zaštitu podataka.</p>

            <p>Ova izjava o privatnosti se odnosi na prikupljanje i objavljivanje ličnih podataka na javno dostupnom veb-sajtu Codeweek.eu, lica koja služe kao kontakt osobe za aktivnosti EU Nedelje programiranja (ambasadori Nedelje programiranja, koordinatori Ministarstva obrazovanja, vodeći nastavnici, kao i organizatori aktivnosti i događaja).</p>



            <h3><strong>2. Zbog čega obrađujemo vaše podatke?</strong></h3>

            <p>Evropska komisija prikuplja i objavljuje vaše lične informacije kako bi potpomogla da zainteresovane strane ili građani identifikuju kontakt osobe. Postavljanje stvarnih osoba kao kontakt osoba je najbolji i najefikasniji način da osiguramo da zainteresovana lica mogu da stupe u kontakt sa službama Komisije.</p>

            <p>Vaši lični podaci se neće koristiti ni za kakvo automatsko odlučivanje, uključujući profilisanje.</p>




            <h3><strong>3. Prema kom pravnom osnovu obrađujemo vaše lične podatke</strong></h3>

            <p>Operacije obrade ličnih podataka za objavljivanje ili kontakt osobe su zakonite prema članu 5(1)(d) Uredbe (EU) 2018/1725 jer ste dali svoj pristanak na obradu ličnih podataka putem veb-obrasca ili kada ste se složili da koristimo vaš imejl i korisničko ime ako ste se prijavili pomoću društvene mreže.</p>



            <h3><strong>4. Koje lične podatke prikupljamo i dalje obrađujemo?</strong></h3>

            <p>Prikupljeni lični podaci su informacije koje olakšavaju vaše identifikovanje kao kontakt osobe i povećavaju vašu vidljivost za javnost, naime: zvanje, ime, prezime, pozicija, poslovna poštanska i imejl adresa, broj telefona, slika, nalog na društvenim mrežama, biografija.</p>

            <p>Ove podatke ste nam dali dobrovoljno, popunjavajući obrazac prijave.</p>

            <p>Navođenje određenih podataka je obavezno da bi bilo omogućeno objavljivanje aktivnosti i/ili kontakt podataka na veb-sajtu codeweek.eu. Ako ne navedete svoje lične podatke, vaša aktivnost ne bi bila objavljena i/ili ne biste mogli da učestvujete u gore navedenim mrežama.</p>

            <p>Sve ostale lične podatke navodite dobrovoljno.</p>



            <h3><strong>5. Koliko dugo čuvamo vaše lične podatke?</strong></h3>

            <p>Komisija čuva vaše lične podatke samo onoliko koliko je to potrebno da bi ispunila svrhu prikupljanja ili dalje obrade opisane u tački 2, naime dok predstavljate kontakt osobu.</p>

            <p>Vaši lični podaci biće uklonjeni sa javno dostupnog veb-sajta čim više ne budete kontakt osoba osim ako ste dali pristanak da budete uključeni u bazu podataka za dalje aktivnosti.</p>



            <h3><strong>6. Kako štitimo i čuvamo vaše lične podatke?</strong></h3>

            <p>Svi lični podaci u elektronskom obliku (imejlovi, dokumenta, baze podataka, otpremljene grupe podataka itd.) se čuvaju ili na serverima Evropske komisije ili njenih ugovarača. Sve operacije obrade se sprovode u skladu sa Odlukom Komisije (EU, Euratom) 2017/46 od 10. januara 2017. godine o bezbednosti komunikacionih i informacionih sistema u Evropskoj komisiji.</p>

            <p>Ugovarači Komisije su obvezani određenom ugovornom odredbom za sve operacije obrade vaših podataka u ime Komisije, kao i obavezama o poverljivosti koje proističu iz transpozicije Opšte uredbe o zaštiti podataka u državama članicama EU (Uredba „GDPR“ (EU) 2016/679).</p>

            <p>Kako bi zaštitila vaše lične podatke, Komisija je utvrdila izvestan broj tehničkih i organizacionih mera. Tehničke mere obuhvataju odgovarajuće radnje po pitanju bezbednosti na mreži, rizika od gubitka podataka, izmene podataka ili neovlašćenog pristupa, uzimajući u obzir rizik koji predstavljaju obrada i priroda ličnih podataka koji se obrađuju. Organizacione mere obuhvataju ograničavanje pristupa ličnim podacima isključivo ovlašćenim osobama sa legitimnom potrebom da znaju podatke u svrhe ove obrade.</p>




            <h3><strong>7. Ko ima pristup vašim podacima i kome se oni razotkrivaju?</strong></h3>

            <p>Pristup vašim ličnim podacima dat je osoblju Komisije koje je odgovorno za sprovođenje ove obrade i ovlašćenom osoblju prema principu „neophodno je  da znaju podatke“. Na takvo osoblje se odnose zakonski, a kada je to potrebno, dodatni ugovori o poverljivosti.</p>

            <p>Konkretno, svim ličnim podacima koje navedete mogu da pristupe administratori veb-sajta (osoblje Komisije), kao i drugo osoblje Komisije koje ima potrebu da zna podatke. . Osim toga, vaše lične informacije i informacije o događajima će biti podeljene sa članovima ambasadora EU Nedelje programiranja i mrežama obrazovnih koordinatora za organizaciju lokalnih aktivnosti ili naknadnih radnji.</p>

            <p>U svrhe povećanja vidljivosti kontakt osobe, vaši lični podaci se objavljuju bez ograničenja pristupa na javnom veb-sajtu: https://codeweek.eu.</p>

            <p><i>Po pitanju prenosa podataka trećim licima</i></p>

            <p>Informacije koje prikupljamo neće biti date nijednom trećem licu, osim u obimu i u svrhe koje mogu biti obavezne prema zakonu.</p>




            <h3><strong>8. Koja su vaša prava i kako možete da ih primenite?</strong></h3>

            <p>Vi imate određena prava kao „subjekt podataka“ prema Poglavlju III (članovi 14-25) Uredbe (EU) 2018/1725, posebno pravo pristupa, ispravke ili brisanja ličnih podataka i pravo da ograničite obradu vaših ličnih podataka. Gde je to primenjivo, imate i pravo da se usprotivite obradi ili pravo na prenosivost podataka.</p>
            <p>Pristali ste da nam date svoje lične podatke za trenutnu obradu i možete povući svoju saglasnost u bilo kom trenutku tako što ćete obavestiti kontrolera podataka. Povlačenje neće uticati na zakonitost obrade koja je sprovedena pre povlačenja saglasnosti.</p>
            <p>Možete da primenite svoja prava tako što ćete se obratiti kontroleru podataka ili u slučaju sukoba, službeniku za zaštitu podataka. Po potrebi, možete se obratiti i evropskom supervizoru za zaštitu podataka. Njegove kontakt informacije su date u okviru Naslova 9 ispod.</p>



