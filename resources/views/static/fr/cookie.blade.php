            <h3><strong>Les cookies, qu’est-ce que c’est?</strong></h3>

            <p>
            <div>Un cookie est un petit fichier texte qu’un site web stocke sur votre ordinateur ou appareil mobile lorsque vous visitez le site.</div>

            <div>
                <ul>
                    <li><strong>Les cookies de premier niveau</strong> sont des cookies définis par le site web que vous visitez. Seul ce site web peut les lire. En outre, un site web peut potentiellement utiliser des services externes, qui mettent également en place leurs propres cookies, connus sous le nom de <strong>cookies tiers</strong>.</li>
                    <li>Les cookies persistants sont des cookies enregistrés sur votre ordinateur et qui ne sont pas supprimés automatiquement lorsque vous quittez votre navigateur, contrairement aux cookies de session, qui sont supprimés lorsque vous quittez votre navigateur.</li>
                </ul>

            </div>

            <p>Lors de votre première visite sur le site web de Codeweek, vous serez invité(e) à <strong>accepter ou à refuser les cookies</strong>.</p>

            <p>Leur intérêt consiste à permettre au site de mémoriser vos préférences (nom d’utilisateur, langue, etc.) pendant un certain temps.</p>

            <p>De cette façon, vous n’avez pas besoin de les saisir à nouveau lorsque vous naviguez sur le site au cours de la même visite.</p>

            <p>Les cookies peuvent également être utilisés pour établir des statistiques anonymes sur l’expérience de navigation sur nos sites.</p>
            </p>


