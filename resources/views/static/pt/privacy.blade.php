            <h1 style="margin-bottom:10px;">PROTEÇÃO DOS SEUS DADOS PESSOAIS</h1>

            <h3><strong>1. Introdução</strong></h3>

            <p>A Comissão Europeia (a seguir designada «Comissão») está empenhada em proteger os seus dados pessoais e respeitar a sua privacidade. A Comissão recolhe e trata dados pessoais ao abrigo do Regulamento (UE) 2018/1725 do Parlamento Europeu e do Conselho, de 23 de outubro de 2018, relativo à proteção das pessoas singulares no que diz respeito ao tratamento de dados pessoais pelas instituições e pelos órgãos e organismos da União e à livre circulação desses dados (que revoga o Regulamento (CE) n.º 45/2001).</p>

            <p>A presente declaração de privacidade explica o motivo por que recolhemos e tratamos dados pessoais, a forma como os recolhemos, processamos e garantimos a sua proteção, o modo como a informação obtida é utilizada e os direitos que lhe assistem relativamente aos seus dados pessoais (o direito a aceder, retificar, bloquear, etc.). Indica igualmente os contactos do responsável pelo tratamento a quem pode dirigir-se para exercer os seus direitos, do encarregado da proteção dos dados e da Autoridade Europeia para a Proteção de Dados.</p>

            <p>A presente declaração de privacidade diz respeito à recolha e publicação de dados pessoais no sítio Codeweek.eu acessível ao público, das pessoas que servem de pontos de contacto para as atividades da Semana Europeia da Programação (embaixadores da Semana Europeia da Programação, coordenadores dos ministérios da Educação, professores proeminentes, bem como organizadores de atividades e eventos).</p>



            <h3><strong>2. Por que motivo tratamos os seus dados?</strong></h3>

            <p>A Comissão Europeia recolhe e publica as suas informações pessoais para facilitar a identificação dos pontos de contacto por parte dos intervenientes ou cidadãos interessados. A disponibilização de pessoas reais como pontos de contacto é a melhor e mais eficaz forma de assegurar que as pessoas interessadas podem entrar em contacto com os serviços da Comissão.</p>

            <p>Os seus dados pessoais não serão utilizados para uma tomada automatizada de decisões, incluindo a definição de perfis.</p>




            <h3><strong>3. Quais os fundamentos jurídicos em que nos baseamos para tratar os seus dados pessoais?</strong></h3>

            <p>As operações de tratamento de dados pessoais para a publicação de pontos de contacto são lícitas ao abrigo do artigo 5.º, n.º 1, alínea d), do Regulamento (UE) 2018/1725, uma vez que deu o seu consentimento ao tratamento dos seus dados pessoais através de um formulário em linha ou quando aceitou que utilizemos o seu endereço eletrónico e nome de utilizador, caso se tenha registado numa rede social.</p>



            <h3><strong>4. Que dados pessoais recolhemos e tratamos posteriormente?</strong></h3>

            <p>Os dados pessoais recolhidos são informações que facilitam a sua identificação como ponto de contacto e aumentam a sua visibilidade para o público, nomeadamente: título, nome, apelido, cargo, endereço postal e endereço eletrónico profissionais, número de telefone, fotografia, conta nas redes sociais, biografia.</p>

            <p>Forneceu estes dados pessoais a título voluntário mediante o preenchimento do formulário de candidatura.</p>

            <p>O fornecimento de determinados dados pessoais é obrigatório para a publicação de atividades e/ou informações de contacto no sítio codeweek.eu. Se não fornecer os seus dados pessoais, a sua atividade não será publicada e/ou não será capaz de participar nas redes acima indicadas.</p>

            <p>Os restantes dados pessoais são fornecidos a título voluntário.</p>



            <h3><strong>5. Durante quanto tempo conservamos os seus dados pessoais?</strong></h3>

            <p>A Comissão conserva os seus dados pessoais apenas durante o tempo necessário para cumprir o objetivo que presidiu à recolha ou ao tratamento dos mesmos, descrito no ponto 2, nomeadamente enquanto servir de ponto de contacto.</p>

            <p>Os seus dados pessoais serão removidos do sítio Web disponível ao público logo que deixe de exercer a sua função de ponto de contacto, a menos que tenha autorizado a sua inclusão na base de dados para atividades futuras.</p>



            <h3><strong>6. Como protegemos e salvaguardamos os seus dados pessoais?</strong></h3>

            <p>Todos os dados pessoais em formato eletrónico (mensagens eletrónicas, documentos, bases de dados, lotes de dados carregados, etc.) são armazenados em servidores da Comissão Europeia ou de empresas por ela contratadas para o efeito. Todas as operações de tratamento são realizadas nos termos da Decisão (UE, Euratom) 2017/46 da Comissão, de 10 de janeiro de 2017, relativa à segurança dos sistemas de comunicação e de informação na Comissão Europeia.</p>

            <p>Os contratantes da Comissão estão vinculados por uma cláusula contratual específica respeitante às operações de tratamento dos seus dados em nome da Comissão e pelas obrigações de confidencialidade decorrentes da transposição do Regulamento Geral sobre a Proteção de Dados nos Estados-Membros da UE (Regulamento (UE) 2016/679 — RGPD).</p>

            <p>A Comissão adotou várias medidas técnicas e organizativas para proteger os seus dados pessoais. As medidas técnicas incluem ações adequadas para garantir a segurança em linha e minimizar o risco de perda, alteração ou acesso não autorizado aos dados, tendo em conta o risco inerente ao tratamento e a natureza dos dados pessoais objeto de tratamento. As medidas organizativas incluem a limitação do acesso aos dados pessoais exclusivamente a pessoas autorizadas com uma necessidade legítima de tomar conhecimento dos dados para efeitos da operação de tratamento.</p>




            <h3><strong>7. Quem tem acesso aos seus dados e a quem podem ser divulgados?</strong></h3>

            <p>O acesso aos seus dados pessoais é facultado a membros do pessoal da Comissão Europeia responsáveis pela execução da operação de tratamento e a membros do pessoal autorizados, em conformidade o princípio da «necessidade de tomar conhecimento». As pessoas em causa estão obrigadas a respeitar os acordos de confidencialidade estatutários e, se necessário, acordos de confidencialidade adicionais.</p>

            <p>Mais concretamente, todos os dados pessoais fornecidos por si podem ser acedidos pelos administradores dos sítios Web (membros do pessoal da Comissão), bem como por outros membros do pessoal da Comissão com base na «necessidade de tomar conhecimento». . Além disso, as suas informações pessoais e informações de eventos serão partilhadas com membros das redes de embaixadores da Semana Europeia da Programação e de coordenadores pedagógicos para a organização ou o seguimento de atividades locais.</p>

            <p>Tendo em vista o aumento da visibilidade dos pontos de contacto, os seus dados pessoais são publicados sem qualquer limitação de acesso no sítio Web público: https://codeweek.eu.</p>

            <p><i>Relativamente à transferência de dados para terceiros</i></p>

            <p>As informações que recolhemos não serão cedidas a qualquer terceiro, exceto na medida e para os efeitos previstos por lei.</p>




            <h3><strong>8. Quais são os seus direitos e como pode exercê-los?</strong></h3>

            <p>Enquanto «titular de dados», assistem-lhe direitos específicos ao abrigo do capítulo III (artigos 14.º a 25.º) do Regulamento (UE) 2018/1725, designadamente o direito a aceder, retificar ou apagar os seus dados pessoais e o direito de limitar o tratamento dos seus dados pessoais. Quando aplicável, também lhe assiste o direito de se opor ao tratamento ou o direito à portabilidade dos dados.</p>
            <p>Embora tenha consentido em fornecer-nos os seus dados pessoais para a presente operação de tratamento, pode retirar o seu consentimento, em qualquer momento, notificando o responsável pelo tratamento. A retirada do consentimento não afeta a licitude do tratamento anteriormente efetuado.</p>
            <p>Pode exercer os seus direitos contactando o responsável pelo tratamento ou, em caso de conflito, o encarregado da proteção de dados. Caso necessário, pode igualmente dirigir-se à Autoridade Europeia para a Proteção de Dados. Os contactos são indicados no ponto 9 abaixo.</p>



